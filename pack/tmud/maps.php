<Br><br>
The Two Towers has painstakingly recreated the lands of Middle-Earth,
which means there are literally hundreds of thousands of rooms for you to
explore.  With such a large area, having a map is essential!  Here are some maps to help you out:
<Br><br>
<h2>Graphical Maps</h2>
<a href="resources/maps/Middleearth.html" target="_map"><b>Color map of Middle-Earth</b></a><br>
A large color map of Middle-Earth, by Thomas Lornsen.
<Br><br><br>
<h2>Starting Town Maps for the Free Races</h2>
<a href="resources/maps/Adornas.html" target="_map"><b>Adornas</b></a><br>
Adornas, western outpost of the Rohirrim.  Starting town for Eorlings.
<br><br>
<a href="resources/maps/Belegost.html" target="_map"><b>Belegost</b></a><br>
Belegost, deep in the Blue Mountains.  Starting town for the Dwarves.
<br><br>
<a href="resources/maps/Bree.html" target="_map"><b>Bree</b></a><br>
Bree.  Starting town for Edain and Dunedain.
<br><br>
<a href="resources/maps/Bywater.html" target="_map"><b>Bywater</b></a><br>
Bywater, also known as Hobbiton, in The Shire.  Starting town for Hobbits.
<br><br>
<a href="resources/maps/Druwaur.html" target="_map"><b>Druwaur</b></a><br>
Druwaur.  Starting town for Dunlendings.
<br><br>
<a href="resources/maps/GreyHavens.html" target="_map"><b>The Grey Havens</b></a><br>
The Grey Havens on the Western shores of Lindon, where the elves prepare
their ships to depart Middle Earth.  Starting town for Sindar Elves.
<br><br>
 <a href="resources/maps/Rivendell.html" target="_map"><b>Rivendell's Last Homely House</b></a><br>
The Valley of Rivendell, containing the Last Homely House of Elrond.
Starting town for the Silvan Elves.
<br><br>
<br>
<h2>Starting Town Maps for the Evil Races</h2>
<a href="resources/maps/Asubuhi.html" target="_map"><b>Asubuhi</b></a><br>
The encampment of Asubuhi.  Starting town of Variags.
<br><br>
<a href="resources/maps/Azrakadar.html" target="_map"><b>Azrakadar</b></a><br>
The sea-side town of Azrakadar.  Starting town of the Black Numenorians.
<br><br>
<a href="resources/maps/EasterlingCamp.html" target="_map"><b>Easterling Camp</b></a><br>
A recently constructed Easterling camp.  Starting town for Easterlings.
<br><br>
<a href="resources/maps/Tavorus.html" target="_map"><b>Tavorus</b></a><br>
Tavorus, in the south.  Starting town of Haradrim.
<br><br>
<a href="resources/maps/Turseg.html" target="_map"><b>Turseg</b></a><br>
The caves of Turseg.  Starting town for all Uruk-Hai.
<br><br>
<br>
<h2>Grid Maps</h2>
<a href="resources/maps/westgrid.html" target="_map"><b>West Arda</b></a><br>
A map of the area west of the Misty Mountains, the starting lands of all
Free Race players.
<br><br>
<a href="resources/maps/eastgrid.html" target="_map"><b>East Arda</b></a><br>
A map of the area east of the Misty Mountains, from Mirkwood down to
Rohan.
<br><br>
<a href="resources/maps/pel.html" target="_map"><b>the Pelennor</b></a><br>
A map of the Pelennor, the battlefield before the gates of Minas Tirith,
where the Battle for the future of Middle Earth is being fought.  It is
here that you can take command of an Army of either Gondor or Mordor and
help win the war!
<br><br> 
<a href="resources/maps/south.html" target="_map"><b>South Gondor</b></a><br>
A map of the area south of the White Mountains, the Stone Land of Gondor.
<br><br>
<a href="resources/maps/mordor.html" target="_map"><b>Mordor</b></a><br>
Mordor, the land of the Dark Lord Sauron.  This area is lawless, and any
player in this land can be Player Killed or Stolen from without any
recourse from the lawsys.  This area is for advanced players!
<br><br>
<a href="resources/maps/nearharad.html" target="_map"><b>Near Harad</b></a><br>
Harad, the lands of the Haradrim.  These are lands controlled by the
lord Sauron, and all people here are under his sway.
<br><br>
<a href="resources/maps/umbar.html" target="_map"><b>Umbar</b></a><br>
A map of the land of Umbar, home of the infamous Corsairs.
<br><br>
<a href="resources/maps/harondor.html" target="_map"><b>Harondor</b></a><br>
A map of the lands of Harondor in the far south.
<br><br>
<a href="resources/maps/farharad.html" target="_map"><b>Far Harad</b></a><br>
A map of the lands of Far Harad, starting area of all Serving Race
players.
<br><br> 
<br>
<h2>Other Maps</h2>
<a href="resources/maps/pelargir.html" target="_map"><b>Pelargir</b></a><br>
A map of the city of Pelargir in South Gondor.
