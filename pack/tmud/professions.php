<br>
On The Two Towers you are not born into a profession, you join one by
finding a trainer during your adventures.  All professions' abilities
are open to all players, but you won't be very successful trying to
perform the abilities of another profession without training.

<br><br>
<br>
<H2>The Common Professions</H2>

<H3>Archer</H3>
Master of the bow, archers deal massive damage but always have
to keep a close count on the number of arrows in their quivers.
Archers can fletch their own arrows, salvage arrows after a fight,
and restring their bow (if the string breaks).  In combat, a
skilled archer can multinock to fire two arrows at once, and
kickback their opponent to get into better position for a lethal
shot.
<br><br>

<H3>Assassin</H3>
Assassins are silent killers for hire.  Their abilities to
move around undetected and conceal their identity put them
in position to deliver a lethal backstab on an unsuspecting
victim.  And if the victim tries to flee, their hunt ability
ensures that they finish what they start.
<br><br>

<H3>Rider</H3>
The Rider is the master of the mount.  Whether riding a Horse or
a Mumakil, they are unmatched on the field of battle.  As symbols
of battlefield superiority they inspire their allies with their
mighty warcries.  Against other mounted opponents they can unseat
their riders, and against those on foot they can use their mounted
combat skills to literally trample their foes.
<br><br>

<H3>Thief</H3>
The Thief is a master of taking things which do not belong to
them.  By hiding, sneaking, and picking locks they can access
areas which others have to fight to get into.  Undetected,
they can steal gold and items from unsuspecting victims.
Their appraise ability lets them know if an item is worth
stealing, or a victim is worth stealing from.
<br><br>

<H3>Warrior</H3>
The Warrior is the master of battle.  Well balanced between
offensive power and the use of armour for defense, the
warrior has the stuff legends are made of.  The combat arms
skill of the warrior allows them to check the condition
of their gear and repair it when needed, as well as
comparing the effectiveness of one piece of gear with another.
Knowing the weak point in a door or chest they can bash it
open, even if locked.
<br><br>


<br>
<H2>Professions Only available to the Free Races</H2>

<H3>Civilian</H3>
All Free Races are born as Civilians.  Civilians have no
extra abilities, although they are able to learn and master
more tradeskills than players who join a profession.  It
takes a talented gamer to rise to the highest rank of
civilian.
<br><br>

<H3>Ranger</H3>
The Ranger is the master of the wilderness.  Their
knowledge of the wild allows them to travel faster
off of the beaten path and even lead a party without
getting them lost.  They can forage for healing herbs
and use their skill in nature's lore to heal either
themselves or their allies.  Protectors of the land,
they can follow the tracks of any intruders and even
set traps to catch them.  At home in the wild they
can easily survey the landscape around them and with
their eaglesight ability they can see even in the
darkest nights.  In combat, their feint attack is
effective and deadly.
<br><br>

<H3>Wizard</H3>
The Wizard is the master of the magical arts.  Not to be confused
with the Istari such as Gandalf which are inately magical, the
wizard must train and study to learn their craft.  Their spells
they are not born with, they must be earned through various
quests and transcribed into the spellbooks.  Once a spell is
learned, they can cast it or enchant it onto an item for
easier casting later.  Their 6th sense skill allows them to find
the location of other players, and their counter ability allows them
to counter another wizard who may be attempting to cast a spell.
The spells available to the wizard range from the offensive,
to the defensive, to the trivial such as fireworks.
<br><br>

<br>
<H2>Professions Only available to the Evil Races</H2>

<H3>Minion</H3>
All those born into Sauron's service start as minions.  Minions
have no extra abilities, although they are able to learn and master
more tradeskills than players who join a profession.  It
takes a talented gamer to rise to the highest minion ranks.
<br><br>

<H3>Sniffer</H3>
Sniffers are the leaders of the hunt.  At home in the wild they
can sniff the direction of their prey and then track them.  They
are often used to lead hunting parties.  Their ability to attune to
the Dark Lord's power allow them to see in the darkest dark or the
brightest of lights.  In combat they are brutally efficient killers
with their shiv ability.
<br><br>

<H3>Necromancer</H3>
Necromancers are masters of the arcane arts.  Named after the
Dark Lord during his stay in Mirkwood, these powerful spellcasters
are capable of conjuring up very powerful spells to further the
Dark Lord's conquest.  Necromancers are able to sacrifice their
own life to restore the life of another, or even imbue their
lifeforce into an object.  They can sense the presence of other
players, and in combat their powerful leech attack drains the
very life from their opponent.
<br><br>
