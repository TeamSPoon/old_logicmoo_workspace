<h2>Connecting</h2>

<p>
To quickly start playing The Two Towers, we recommend that you use our web client.
The client is available under the "Play Now" button in the upper left corner of
the page, or under <a href="client/simple/simple.php">this link</a>.
</p><p>
Advanced MUD players may wish to pick a <a href="clients.php">specialized MUD client</a> instead. Clients have many features, such as triggers, aliases, scripts and even auto-mappers.  Most players use 
<a href="http://www.gammon.com.au/mushclient/">MUSHclient</a> or
<a href="http://www.zuggsoft.com">zMUD</a>.
</p>
<p>
You may also use a simple <a href="telnet://t2tmud.org:9999">telnet</a> connection.
</p>

<h2>Creating Your First Character</h2>

<h3>Choosing a Name for Your Character</h3>

<p>
At the login prompt, enter the name "new" to create a new character.
Next, you will be asked to choose a name for your character.  Please 
choose one that's fitting to Tolkien's world, and avoid names that are 
real words or common modern names.  We do not allow legendary character names,
such as Legolas or Galadriel.  If your name is deemed not acceptable by the 
staff, you will be required to choose another before advancing. 
</p>

<h3>Choosing a Password</h3>

<p>
You will also be required to choose a password.  Please make it something
that you can remember easily.  Never give out your password to anyone.  
No one, including Ainur (the staff who maintains the game) should ever 
ask you for your password.  Keep it secret.  Keep it safe.
</p>

<h3>Inputting Your Real Name and Email Address</h3>

<p>
During the character creation process you will be given a prompt to add your 
real name and email address to our database. We do not sell or give out email 
addresses to outside sources.  You need to provide a real email address in the
event you lose or forget your password and need to recover it.  If you create 
a fake email address, it will come back to haunt you if you are ever stuck 
with a password problem.
</p>

<h3>New Players</h3>

<p>
You will receive a prompt asking if you are new to The Two Towers.  
Answer yes to register yourself as a new character.  
Areas of the game will now provide some additional instructions to guide you 
through your adventures, and our new player Helper staff will be better able
to assist you.
</p>

<h3>Facebook</h3>

<p>
If you are connecting to the game while logged on to Facebook, you will be 
prompted whether you want the game to post information about your character's
progress on your facebook wall.  Answering yes to this question will make 
the MUD display a prompt for you each time you achieve something important in
the game, such as gaining a new level.  Note that you can always change this
preference later (or even decide which events should send messages) in game 
through the <a href="mudhelp/settings.php">"settings" command</a>.
</p><p>
You can determine if you are logged in to facebook by checking the last 
button in the menu in the lower part of the client display - if it says 
"[f] Login", it means you are not connected.  If it says "[f] Logout", you are.
</p>

<h3>Selecting Your Character's Gender</h3>

<p>
The next step in the character creation process will be to pick your 
character's gender.
</p>

<h3>Choosing a Side</h3>

<p>
Type "create" to continue with your character creation.  You will now be 
asked to choose a side in the war.  Those who choose "service" are born 
in servitude to the Dark Lord Sauron and begin in the southern lands.  
Those who choose "freedom" are born free and begin west of the Misty Mountains. 
The side you choose will determine which races are available for you to 
choose from.  For example, a dwarf can not be born into servitude.  If you
choose "service" the dwarf race will not be an option.
</p><p>
Note: The "service" (evil) side is considered to be more challenging, and
therefore is not recommended for truly new players.
</p>

<h3>Selecting a Race</h3>

<p>
The final step in the character creation process is to choose a 
<a href="mudhelp/races.php">race</a>.  There are several races 
you can choose from on either side.
</p>

<h2>The Tutorial</h2>
<p>
Now that you have a character, you will be moved into the tutorial area.  
It is highly recommended that you go through the tutorial even if you
have played the game before.  The tutorial gives your character some
handy "autoloading" equipment (an item that will stay with you through
logons) which will make playing a little more convenient.  If you still 
do not want to take the tutorial, though, you can type "skiptutorial" at 
any time to exit it. 
</p><p>
During the tutorial, a helpful ranger (or a sniffer, if you chose an Evil 
Race character) will teach you some basics about the game. If you have 
played online text games before, this may be a little elementary for you. 
However, it only takes about twenty minutes and will help you learn some 
of the basic commands on The Two Towers. 
</p>

<h2>Hints</h2>

<p>
Once you have completed the tutorial, you will be free to go out on your
own and explore Arda.  At this time, hints will automatically turn on.  We recommend
that you keep hints on if you are new to the game.  However, if you would 
like to turn them off, simply type "hint off".
</p>

<h2>Help Files</h2>

<p>
The Two Towers has extensive help files to aid you.  You may view them by 
using the "help" command in the game window (type "help help" for a quick 
reference on how that works if you are unsure) or by using the help files 
available on the website and this help window. 
<p></p>
For a list of help files type "help topics" in game, or click 
<a href="mudhelp/topics.php">here</a>.  You can also use "help commands"'
as a quick reference guide for common commands in The Two Towers. This list 
is also available <a href="mudhelp/commands.php">here</a>.
</p>

<h2>Helpers</h2>

<p>
Helpers are characters who have volunteered to help you along with your 
travels. If you have questions or get stuck, please type 'who helper' and 
see the list of helpers online. You can contact a helper by typing:
tell (their-name-here) and your message and pressing ENTER. 
</p><p>
Example: <i>tell Lyneah How do I buy food?</i>
</p><p>
You may also ask a question on the newbie channel (comm) by typing:
</p><p>
<i>] Hello all.  How do I get to Bree?</i>
</p><p>

<h2>Professions</h2>

<p>
You are not asked to pick a <a href="mudhelp/professions.php">profession</a>
when you create your character.  There are many trainers throughout Arda that 
you can study with.  You also have the choice of advancing to the highest level
as a civilian.  Take your time exploring and choose a profession when you are ready.
</p>

<p>
<i>We wish you a safe journey and hope you have fun on your adventures in Middle Earth!</i>
</p>

