<H2>Professional Skills</H2>
As a player on The Two Towers you have 8 slots for professional
skills.  Your primary profession takes up 4 of these skill slots.  The
other 4 slots can be filled from any of the skills of any other profession.
<br><br>
The combinations are limited only to your imagination!
You can be a Warrior/Wizard.  You can be an assassin who also
steals like a thief.  You can mix and match the various professional
skills to create a custom character of your own design.
<br><br>
Skills not of your current profession will slowly degrade over
time.  Skills increase with use, and can also be trained at a
trainer.  So to keep your multi-classing skills at their peak you
will need to be actively using those skills or regularly training
them.
<br><br>
<br>
<H2>Common Skills</H2>
In addition to the 8 professional skills, each player also has
several common skills.  Just like the professionals skills,
these common skills increase with use or can be trained by a 
trainer.
<br><br>
<H3>The common combat skills are:</H3>
<br>
<b>Aim</b> - increases your chance of hitting an opponent<br>
<b>Attack</b> - increases the amount of damage you do when you hit<br>
<b>Dodge</b> - decreases your chance of beign hit by an opponent<br>
<b>Defense</b> - decreases the amount of damage you take when hit<br>
<br>
<H3>Other common skills include:</H3>
<br>
<b>Awareness</b> - increases your chance of detecting that you have
been stolen from and helps you find hiding thieves<br>
<b>Fire building</b> - increases your chance of building a fire when
using the camp ability<br>
<b>Intuition</b> - increases your success when investigating a victim
of a crime to determine the identity of the criminal<br>
<b>Evasion</b> - increases the chance and duration of using the evade ability to evade a hunting attacker<br>
<b>Riding</b> - increases the speed at which you can ride a mount<br>


