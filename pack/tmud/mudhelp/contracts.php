<h2>Contracts (PvP Help)</h2><p>Some of the shadier NPCs stationed around Arda will be helpful if you
ask them about contracts.  This system has been designed so that anonymous
contracts can be put out on anyone.  The money must be presented up front
when you speak to one of the NPCs.  That money will stay with the NPC
until either the issuer cancels the contract, the contract is fulfilled,
or the victim pays off the contract (ie. bribes the contractor NPC to
forget about it).  In order to complete a contract, an assassin must go to
one of these NPCs and officially claim the 'hit' to be able to fulfill the
contract and get the money.  Once this is done, the assassin that claimed
the contract can then claim his money from the NPC.
</p><p>The contract system has the following additional features:
</p><ul><li>The issuer of the contract can cancel the contract if they change 
their mind.  A contract can only be cancelled if there are no assassins
currently working on it.  Also, cancelling a contract incurs a 20% fee
from the contractors, to compensate for their time and effort spent.
</li><li>The victim can pay off the contract to make the contractor forget about
it.  The price is 5 times the original amount offered by the contract
issuer.
</li><li>The issuer may leave a message that the victim will see when the 
contract is fulfilled.  Please note that contract messages are
subject to profanity and harassment rules and abuses will be dealt
with accordingly.  There is a 200 character limit on contract
messages.
</li><li>The contractor NPCs sell 'contract masks' for assassins.  When worn,
a mask will hide your true name from your victim (and only them),
making it harder for them to figure who was the killer.  It takes
5 minutes for the contractor to prepare the mask; the cost varies
based on your and your victim's level.
</li><li>The contractor NPCs can 'locate' contract victims for assassins;
this acts similarly to a wizard's 'find' ability.
</li><li>Contracted players who have their PVP flag set to 'on' will not be
able to set their flag to 'merciful' while the contract persists
</li></ul><p>Many checks and balances have been placed into the contract system to
prevent abuse.  In addition to the above mentioned features, the following
applies:
</p><ul><li>Only non-assassins can place contracts.
</li><li>Only assassins can claim and fulfill contracts.
</li><li>Only one contract can be placed at a time per character.
</li><li>A minimum 1d age requirement applies to use the contract system.
</li><li>A contract has to 'mature' for a set time period, once issued, before 
it can be claimed.
</li><li>There is a time delay between being able to re-issue a contract.
</li><li>Fines are significantly decreased for contracted playerkills.
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
