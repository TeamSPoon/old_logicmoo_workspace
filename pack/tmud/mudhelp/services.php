<h2>Services (Services Help)</h2><p>In the cities and towns of Arda you will find many helpful NPCs that
offer services to players.  While there are many quest NPCs whose behaviour
is strictly tied to their quests, there are several 'generic' services
that can be found almost everywhere.  These include:
</p><ul><li><span style="font-weight:bold"><a href="trainers.php">trainers</a></span> - The NPCs that can advance your stats and skills, as well as
admit you to their professions.  By far the most important service in
Arda!
</li><li><span style="font-weight:bold"><a href="shops.php">shops</a></span> - Almost every town in Arda has at least one functional shop -
a place where adventurers like you may sell and buy useful (and
not-so-useful as well) items.
</li><li><span style="font-weight:bold"><a href="banks.php">banks</a></span> - Scattered around Arda are several banks that can hold your
excess money, for a small fee.  The bank accounts are tied, so you
may deposit gold in one bank and withdraw it in another.
</li><li><span style="font-weight:bold"><a href="inns.php">inns</a></span> - There are many inns in Arda, offering rooms and beds to weary
travelers.  In addition to providing a safe place to sleep, inns can
also rent you a strongbox where you can keep your valuable items.
</li><li><span style="font-weight:bold"><a href="bars.php">bars</a></span> - Places of fun and entertainment, the bars of Arda offer drinks
and food to the thirsty and the hungry.
</li><li><span style="font-weight:bold"><a href="healers.php">healers</a></span> - The healers that reside across towns in Arda are very much
sought after; they are the only ones capable to quickly restore lost
health so that adventurers may complete their quests faster.
</li><li><span style="font-weight:bold"><a href="auction.php">auction</a></span> - The four auction houses in Arda are places where players 
may trade items with each other.  The trade goes through an NPC to
ensure that it is fair.
</li><li><span style="font-weight:bold"><a href="mailrooms.php">mailrooms</a></span> - Every major town of Arda has a mailroom: a place where
you can send and receive mail from other players and Ainur.
</li><li><span style="font-weight:bold"><a href="courthouses.php">courthouses</a></span> - Courthouses are places where the lawsys (PvP) reporting
takes place.  A courthouse is also the only place where you can switch
your PvP flag.
</li><li><span style="font-weight:bold"><a href="stables.php">stables</a></span> - Stables located in most towns in Arda allow you to safely
stable your horse, protecting it from being killed by mischievous
players.
</li><li><span style="font-weight:bold"><a href="smiths.php">smiths</a></span> - Weaponsmiths and armoursmiths may fix your broken equipment
back into usefulness.
</li><li><span style="font-weight:bold"><a href="armour_traders.php">armour traders</a></span> - NPCs who can replace one piece of armour for another,
which may be more suited for your roleplay.
</li><li><span style="font-weight:bold"><a href="cemeteries.php">cemeteries</a></span> - Places where ghosts can return back to life.
</li><li><span style="font-weight:bold"><a href="memorials.php">memorials</a></span> - A special service dedicated to preserving the memory of 
players who have left Arda.
</li></ul><p>To learn more about a particular service, type 'help &lt;service name&gt;'.
</p><p>For almost all of the above services, the following restrictions apply:
</p><ul><li>Services in Free Race towns will refuse to deal with Evil Race players,
and vice-versa.  Seek a service on your own side.
</li><li>If you kill a lot of citizens in a given town, services in that town
will increase prices for you.  See '<a href="kills.php">help kills</a>' for more details.
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
