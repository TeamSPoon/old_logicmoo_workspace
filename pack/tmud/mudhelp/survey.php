<h2>Survey (System command)</h2><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
<td style="border:0px" valign="top"><span style="font-weight:bold">Usage:</span></td>
<td style="border:0px" valign="top">
<pre style="margin:0px;padding;0px;margin-left:10px">survey &lt;command&gt;</pre>
</td></tr></table>
<p>The survey command provides player access to the survey system.
Surveys are used by the administration to poll for player opinion on
important gameplay matters.  Please do not hesitate to use the system
to voice your opinion; feedback is important!
</p><p>The view the surveys, you can use the following commands:
</p><ul><li><span style="font-weight:bold">survey list</span> - list all active surveys
</li><li><span style="font-weight:bold">survey all</span> - list all surveys
</li><li><span style="font-weight:bold">survey waiting</span> - list the current surveys
</li><li><span style="font-weight:bold">survey done</span> - list the completed surveys
</li><li><span style="font-weight:bold">survey todo</span> - list the surveys you haven't voted on
</li><li><span style="font-weight:bold">survey new</span> - list if any surveys have been updated/new
</li><li><span style="font-weight:bold">survey new #</span> - read only the new comments for a survey
</li><li><span style="font-weight:bold">survey new waiting</span> - list any waiting surveys that have been updated
</li><li><span style="font-weight:bold">survey mark read</span> - mark all surveys as read
</li><li><span style="font-weight:bold">survey next</span> - read the next unread survey
</li><li><span style="font-weight:bold">survey read #</span> - read the survey
</li><li><span style="font-weight:bold">survey read next</span> - read the next survey submitted after the current
</li><li><span style="font-weight:bold">survey read previous</span> - read the previous survey submitted to the current
</li><li><span style="font-weight:bold">survey read current</span> - read the survey you have just read
</li><li><span style="font-weight:bold">survey short #</span> - read just the results without the comments
</li><li><span style="font-weight:bold">survey feedback</span> - list all surveys with Valinor feedback
</li><li><span style="font-weight:bold">survey feedback next</span> - read the next survey with unread Valinor feedback
</li><li><span style="font-weight:bold">survey announce new</span> - be notified about unread surveys upon login
</li><li><span style="font-weight:bold">survey announce todo</span> - be notified about todo surveys upon login
</li><li><span style="font-weight:bold">survey announce off</span> - turn off login alerts for new surveys
</li></ul><p>To search the surveys, you can use the following commands:
</p><ul><li><span style="font-weight:bold">survey search &lt;x&gt;</span> - search for &lt;x&gt; in active surveys
</li><li><span style="font-weight:bold">survey search &lt;state&gt; &lt;x&gt;</span> - search for &lt;x&gt; in all surveys that are in
state &lt;state&gt;  e.g. 'survey search done hi' will search all
finished surveys for the text 'hi'.
</li><li><span style="font-weight:bold">survey search all &lt;x&gt;</span> - search ALL surveys for &lt;x&gt;
</li></ul><p>To post your feedback, you can use the following commands:
</p><ul><li><span style="font-weight:bold">survey approve #</span> - vote your approval for a survey
</li><li><span style="font-weight:bold">survey reject #</span> - vote your rejection of a survey
</li><li><span style="font-weight:bold">survey abstain #</span> - abstain on a survey
</li><li><span style="font-weight:bold">survey comment post #</span> - post a comment on a survey
</li><li><span style="font-weight:bold">survey comment read #</span> - list all comments on a specific survey
</li><li><span style="font-weight:bold">survey comment new #</span> - list only the new comments for a survey
</li><li><span style="font-weight:bold">survey comment next</span> - read next survey's comments
</li><li><span style="font-weight:bold">survey comment help</span> - display the comment commands
</li></ul><p>You may also 'approve current' 'reject current' and 'abstain
current', which will make you vote on the current survey that you have
just read.
