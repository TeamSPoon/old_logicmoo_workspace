<h2>Combat (Combat Help)</h2><p>Combat is one of the essential features of gameplay on The Two Towers.
While there is a lot of other things to do in Arda, you will find yourself
fighting fairly often.  Depending on your character's morality, you may
want to kill Sauron's minions who threaten the Free Races of Arda, or you
may choose to slaughter innocent town inhabitants for the glory of the
Dark Lord instead.  Or you can just kill everyone you want for their
gold - it is all up to you!
</p><p>For the most part, combat on The Two Towers is automated: once it
is initiated, you will continue fighting until one of the combatants
dies or flees.  The automated combat is turn-based; in each turn, each
combatant attempts to hit one of their opponents, and a message is
given on whether they hit (and how strong) or miss.  The delay between
turns is roughly two seconds.  No actual numbers are printed during the
duration of combat; we have chosen that to allow for more immersive
display of combat rather than reduce it to pure dice rolling.
</p><p>While the automated combat may sound boring, do not think it is so
simple.  There are a lot of abilities and commands available to you that
can be used during combat to give you an advantage.  Furthermore, there
are many items with special combat usage that can greatly change combat
experience.
</p><p>If you want to learn more about combat, you should also check the
following help files:
