</li><li><span style="font-weight:bold"><a href="critical_hits.php">critical hits</a></span> - The most powerful (and rare) hits there are.
</li><li><span style="font-weight:bold"><a href="abilities.php">abilities</a></span> - List of professional abilities, many of which can only
be used in combat.
</li><li><span style="font-weight:bold"><a href="wimpy.php">wimpy</a></span> - Allows you to automatically flee combat on low HP.
