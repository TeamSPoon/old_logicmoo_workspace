<h2>Valinor (Rules and Policies)</h2><p>In Tolkien's world (see especially the Silmarillion), immortal beings -
gods, if you will - are called "Ainur".  A single god is an Ainu.
</p><p>Tolkien's Ainur were divided into two broad groupings based on their
relative power and rank:  Valar (the most powerful) and Maiar (less
powerful).  A single god of each rank is called a Vala or a Maia.
</p><p>In The Two Towers, those who 'run' the MUD are Ainur... the symbolism
is fitting.  Just as Tolkien's Ainur created Middle Earth through the
singing of Eru's Great Song (the Ainulindal&euml;), so our Ainur create Middle
Earth through coding and descriptions of new areas, as well as by
maintaining existing areas.
</p><p>Within The Two Towers, we have refined Tolkien's two-rank system to
include four ranks.  Those ranks, and the associated duties, are:
</p><ul><li><span style="font-weight:bold">Maiar</span> - Coders &amp; describers.  The majority of Ainur are Maiar.  
These are the "sharp point of the spear" of the Ainur
community, those who actually create Arda for players.
</li><li><span style="font-weight:bold">Overlords</span> - MUD Leaders, each tasked with ensuring the quality and
success of a functional area.  The functional areas are
General Quality Control (code and descriptions), Balance
and Theme.  There are usually between four and five
Overlords.
</li><li><span style="font-weight:bold">Powers</span> - The Administration, those ultimately responsible for the 
management of Arda.  Their functional areas include User
Code, System Code, Production, Quality Control, Law and
Administration.  There are generally three to five Powers.
</li><li><span style="font-weight:bold">Valar</span> - The founders of The Two Towers and most senior of all Ainur, 
ever watchful yet mostly retired.
</li></ul><p>Maiar and Overlords (yes, there are "Overladies", but we aren't quite
that politically correct!) are considered the two ranks of Maiar.  Powers
and Valar are the two ranks of Valar (in case you were wondering how to
map our ranks to Tolkien's).
</p><p>In addition to these ranks, there are a few "special status"
designations for Ainur:
</p><ul><li><span style="font-weight:bold">Maker</span> - The 'know-it-alls' of coding.  These are the folks that 
maintain and create new commands and other non-area specific
projects (such as coding the lawsys).
</li><li><span style="font-weight:bold">Elder</span> - Elders are members of Valinor who have earned, through their
long term contributions, the right to remain in Valinor even
though they may temporarily or permanently be retired, and
no longer contributing.
