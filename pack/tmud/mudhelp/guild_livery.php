<h2>Guild livery (Guild Help)</h2><p>The guild livery system enables your guild to have its own set of
armour.  A room must be purchased that comes with an armour trader NPC
that will aid in the exchange of armour.  A guild member may then bring
any piece of armour found in Arda, and exchange it for a guild armour
(subject to what the GM has purchased to be available, and of course the
power of the item).
</p><p>There are 10 different pieces of armour a guild may have: helm,
gloves, gauntlets, breastplate, shirt, leggings, boots, chausses, cloak,
and shield.
</p><p>A piece of equipment may be made in the style of elven, dwarven, or
man-made origin.  They are all equivalent in protectiveness and weight,
but players of the same race as the origin will receive a bonus when
wearing them.
</p><p>You may also have pieces of high, great, medium, fair, and low grade.
While you may trade a higher-grade piece of equipment for a lower-grade
piece, you may not trade up.
</p><p>A guild must purchase each specific type and grade of equipment in
order for a guildmember to be able to trade for it.  For example, if the
only piece that you ever purchase is a high-grade man-made breastplate,
you will never be able to trade for a helm.  If you only purchase a helm,
you will only be able to trade for a helm.  You are able to trade a
breastplate for a helm, but the helm will be of lesser protection.
</p><p>In order to set and modify the description of each piece, you must
use the GMedit system.
</p><p>Equipment pieces are traded for set values which means that all
medium-grade elven helms have the same values.
</p><p>Prices for the livery system are as follows:
</p><pre>                           Purchase  Rent
ArmourTrader room+NPC       5,000    5,000
"low" quality armour          200
"fair" quality armour         400
"medium" quality armour       600
"great" quality armour        800
"high" quality armour       1,000
