<h2>Playerkilling (PvP Help)</h2><p>The Two Towers allows playerkilling.  It is, however, limited only to
players who have their PvP flag set to on.  Even within the PvP-on group,
though, playerkilling is restricted.  A player that either murders or
attempts to murder another player can be fined, with a portion of the fine
going to the victim for restitution.  See '<a href="lawsys.php">help lawsys</a>' for full details
about the in-game law system.
</p><p>There are areas in the game world which have been declared unlawful
and aren't subject to the in-game laws or fines.  Currently, these areas
are Mordor and Minas Morgul.  Players should only enter these areas if
they accept that they may be killed without restitution.
</p><p>Even though the in-game laws should keep the playerkillers in order,
there are times when playerkilling may cross the bounds of harassment.
These cases include:
</p><ul><li>Any method of killing another player by a means that circumvents
the lawsys and therefore does not yield fines (bug abuse)
</li><li>Killing or causing the death of a new player (newbiekilling)
</li><li>Repeatedly killing the same player (levelbashing)
</li><li>Killing a lot of players in a short time (spreekilling)
</li></ul><p>While the two latter cases are largely taken care of by the Blessing
of Mandos system, they will still be punishable should one find a way
around the blessing.
