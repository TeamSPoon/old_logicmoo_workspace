<h2>Facebook (Website Help)</h2><h3>Facebook app</h3><p>The Two Towers is available as a facebook application under the
following address:
</p><p><a href="http://apps.facebook.com/thetwotowers"target="_blank">http://apps.facebook.com/thetwotowers</a>
</p><p>The app is just an embedded web client from our main page; if you
prefer to play the game on full screen, there is a button available in
the app's interface that links to the client.
</p><p>Apart from the app, The Two Towers has its own facebook wall, on
which we post all the news and updates that appear on the MUD.  You can
access it at:
</p><p><a href="http://www.facebook.com/apps/application.php?id=320769710036"target="_blank">http://www.facebook.com/apps/application.php?id=320769710036</a>
</p><h3>Facebook messages</h3><p>Since we are a registered Facebook app, The Two Towers has the option
of posting messages to your facebook wall to let your friends know how
you are faring on the MUD (and that you are playing it!).
</p><p>The messages will naturally only work if you are connecting through
the app, or through the web client while being connected to facebook -
we even have a facebook login button on the client page to make it easier.
The facebook messages are opt-in, which means that you must specifically
agree to enable them; we hope you do so to help us promote the MUD around
the Internet!
</p><p>Message posting are enabled through the "facebook" environment
variable.  You may set it with "set facebook on", by using the settings
command, or by using the web client GUI - all work the same.  Once enabled,
the MUD will post messages to your wall:
</p><ul><li>At the end of the tutorial, for new players.
</li><li>When you change your profession.
</li><li>When you gain a level.
</li><li>When you join a guild or become a guildmaster.
</li><li>Every time you complete 25 new quests in the quest tracking system.
</li></ul><p>You may turn off posting of each message type separatedly through
appropriate environment variables; see "<a href="sets.php">help sets</a>" for details or simply
use the "settings" menu / web GUI.
