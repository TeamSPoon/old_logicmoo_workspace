<h2>Mounts (Objects Help)</h2><p>Mounts are the primary transportation option in Arda, capable of
bearing riders and burdens, and you will find that there are plenty
available.  You may simply buy a mount, complete a quest to gain one,
or even tame certain wild animals you encounter in the wilderness.
</p><p>All mounts in the game share the same commands; most of them are only
available on *your* mounts, though.
</p><h3>Commands that you can use while not mounted</h3><ul><li><span style="font-weight:bold">tame &lt;mount&gt;</span> - You can tame certain wild animals (horses, for example)
for your own use.
</li><li><span style="font-weight:bold">lead &lt;mount&gt;</span> - Lead the mount.  This makes the mount follow you as you
walk through rooms.  Mounts may enter most 'outside' rooms in Arda;
however, you will generally be unable to lead them to indoor areas (with
the exception of stables).  If you enter a room where the mount cannot
follow you, you will be notified with an appropriate message.  When you
come back to your mount, you will start leading it again automatically.
</li><li><span style="font-weight:bold">lead</span> - Stop leading your mount.
</li><li><span style="font-weight:bold">mount &lt;mount&gt;</span> - Start riding the mount.  While astride a mount, you will
move together with it, similarly to leading.  And, as with leading, you
will not be able to ride in indoor areas -- attempting to do so will
make you dismount automatically.  Note that there are certain areas in
the game you can lead a mount into but not ride into, such as dense
forests.
</li><li><span style="font-weight:bold">hitch &lt;mount&gt;</span> - Hitch the mount to a hitching post, a specially coded
object which is meant to keep mounts from wandering off when left
unattended.  (See '<a href="stables.php">help stables</a>' for more about storing mounts.)
</li><li><span style="font-weight:bold">unhitch &lt;mount&gt;</span> - The reverse of the above.
</li></ul><h3>Commands that you can use while mounted</h3><ul><li><span style="font-weight:bold">spur/whoa</span> - Increase or decrease the mount's speed.  The higher you set
the speed, the less time you will spend on long 'do' commands and
traveltos.  However, riding fast makes your mount tired, which in turn
makes it slow down again.
</li><li><span style="font-weight:bold">dismount</span> - Get off the mount.  If you have the 'autolead' setting set to
'on', you will start leading it right after dismounting.
</li></ul><h3>Additional commands</h3><ul><li>The 'mount' command has many sub-commands through which you can interact
with the mounts you own.  See '<a href="mount.php">help mount</a>' for more details.
</li><li>You may 'give' a mount to another player, permanently transferring
ownership.
</li><li>You can 'give' food to your mount to heal its wounds and endurance.
</li></ul><h3>Mount 'levels'</h3><p>Through the 'mount evaluate' command, you can gauge how good a
particular mount is.  The better the result, the stronger your mount
will be in combat, and the more endurance it will have -- it will tire
slower.  From best to worst, these are the mount levels:
</p><ul><li>Horse is an extraordinary mount.  You will not find a better mount
anywhere.
</li><li>Horse is magnificent!  Strong, fast, and unafraid of combat, it is a true
prize!
</li><li>Horse is well-trained and will serve its master well in both travel and
combat.
</li><li>Horse is a very good mount, with advanced training.
</li><li>Horse is a good mount, moderately trained.
</li><li>Horse is an average mount, and could use much more training.
</li><li>Horse is very inexperienced and won't be able to fight or run for very
long.
</li></ul><h3>Combat</h3><p>Apart from very special quest-obtained mounts, your mount will not
take a direct part in any of your fights.  However, if you are astride it,
it will 'shield' you, taking part of the damage.
</p><p>As for you, the rider, you will find that while on a mount you can
only use some weapons effectively.  They need to be fairly long in order
to do significant damage to your opponent.  When you have such a weapon,
your mounted combat skill will factor heavily in the ongoing fight.
Experienced riders with proper weapons can be very dangerous when mounted.
</p><p>When your mount is seriously injured, there is a chance that you will
be thrown off.  It can be a painful experience -- you may lose some HP and
become 'busy' for several rounds of combat.  Only a high balance skill can
lessen the effect of falling.  The most skilled riders are even known to
be able to re-mount immediately.
</p><h3>PvP combat</h3><p>You may attack any mount.  Bear in mind, though, that killing
someone's mount usually counts as a 'theft' crime against them, for which
you can be fined.  Exceptions:
</p><ul><li>All mounts in Mordor are attackable without penalty.
</li><li>You can freely attack mounts belonging to a guild that your guild is in
a state of mutual war with.
</li><li>If you are sparring with a person and kill their mount, it will not
be reportable as theft.
</li></ul><h3>Interaction with the environment</h3><p>You will find that certain actions are not possible while mounted.
For example, climbing will usually not be an option.  However, you will be
able to pick up items from the ground; it is assumed that you just quickly
dismount your animal, get the item, and mount again.
</p><h3>Customization</h3><p>There are a few settings (environment variables) that control how
mounts work for you.  See '<a href="sets.php">help sets</a>', or use the 'settings' command for
more details.
