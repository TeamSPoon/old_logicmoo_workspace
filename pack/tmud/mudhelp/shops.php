<h2>Shops (Services Help)</h2><p>Shops are the core of Middle-Earth trade.  They are conveniently
located throughout the world to provide you with an easy form of selling
and buying.  Most shops are run by a non-player character, but there are
other types.  A few shops allow you to trade your goods for something
you'd rather have, and rumour has it that there are even some shops in
need of a manager.
</p><p>These are the general commands for most shops:
</p><ul><li><span style="font-weight:bold">list</span> - shows you a list of everything that is for sale.
</li><li><span style="font-weight:bold">list &lt;w | weapon | a | armour&gt;</span> - list all weapons or armour for sale.
</li><li><span style="font-weight:bold">list &lt;item&gt;</span> - lists all items of given type, eg. 'list sword'.
</li><li><span style="font-weight:bold">list &lt; 50</span> - list everything less than 50 gold.  Can be combined with 
the above.
</li><li><span style="font-weight:bold">handle &lt;item&gt;</span> - lets you handle an item to examine it more closely.
</li><li><span style="font-weight:bold">return &lt;item&gt;</span> - return the item you are handling.
</li><li><span style="font-weight:bold">buy &lt;item&gt;</span> - buy an item from the shop.
</li><li><span style="font-weight:bold">sell &lt;item&gt;</span> - sell an item in your inventory.
</li><li><span style="font-weight:bold">sell &lt;item&gt; from &lt;container&gt;</span> - sell an item from a container you have.
</li><li><span style="font-weight:bold">sell &lt;item&gt; except &lt;item&gt;</span> - sell items excluding certain items.
</li><li><span style="font-weight:bold">sell &lt;item&gt; from &lt;container&gt; except &lt;item&gt;</span> - you get the idea.
</li><li><span style="font-weight:bold">value &lt;item&gt;</span> - same format as 'sell'.  Tells how much you would get
without actually selling the item.
</li></ul><p>In the above, &lt;item&gt; can be:
</p><ul><li>name of an object, like 'sword'.
</li><li>'all', referring to all items you carry / all contents of a container.
</li><li>'all &lt;name&gt;', referring to all objects with given name, for example
'all sword'.
</li><li>'&lt;name&gt; &lt;number&gt;', referring to particular item in your inventory/shop,
such as 'sword 2' or 'helmet 3'.
</li><li>'it' or 'last handled'; used only for 'buy' command to let you buy the
item you've just handled and returned.
</li></ul><p>Apart from these, some shops may have unique commands.  Generally,
there will be a sign in those shops that describes how to use the special
commands.  Some shops support an 'order-by-number' system, where you
simply 'buy #' to purchase that item.  Such shops are rare, and it is near
impossible to run one out of business by buying all its wares.
</p><p>Certain shops specialize in certain items, such as weapons, armour,
musical instruments, etc.  This means they may give better prices for
such items, or only deal in those types of items.  It is a good idea to
investigate many shops to find the best places to buy and sell different
types of items.
