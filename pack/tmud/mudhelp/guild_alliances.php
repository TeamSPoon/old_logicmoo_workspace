<h2>Guild alliances (Guild Help)</h2><p>A system exists where guilds can rank each other according to their
likes and dislikes.  Improved relations turn into alliances, and degraded
relationships eventually fall into a state of warfare.
</p><p>Only the GuildMaster may rank other guilds, and the rankings are as
follows:
</p><ul><li><span style="font-weight:bold">Ally</span> - the GM considers the other guild an ally.
</li><li><span style="font-weight:bold">Neighbor</span> - the GM is on good terms with the other guild.
</li><li><span style="font-weight:bold">Impartial</span> - no preference.
</li><li><span style="font-weight:bold">Disliked</span> - the GM does not like the other guild
</li><li><span style="font-weight:bold">Hostile</span> - the GM has sanctioned actions against the other guild.
</li><li><span style="font-weight:bold">Enemy</span> - the GM has declared open warfare against the other guild.
</li></ul><p>The ranks affect the lawsys penalties when a guild member commits a
crime against a member of a ranked guild:
</p><ul><li><span style="font-weight:bold">Ally</span> - crimes against an ally are fined at +200% the normal rate.
</li><li><span style="font-weight:bold">Neighbor</span> - crimes against a neighbor are fined at +50% fines.
</li><li><span style="font-weight:bold">Hostile</span> - the guild (the guildvault) will pay 20% of the fine.
</li><li><span style="font-weight:bold">Enemy</span> - the guild (guildvault) will pay 100% of the fine.
</li></ul><p>Only the crimes of murder, attempted murder, and attempted theft are
discounted against an enemy guild.  Only the crime of murder is discounted
against a hostile guild.
</p><p>Additionally, any crimes committed against an enemy, as well as any
murders committed against hostile-ranked guildmembers, will not count
against the player's lawsys record.
</p><p>A GM may only declare war on one other guild at a time.  The guild is
notified that they have been declared war upon.  To declare war costs 1,000
gold per member, and will be added to the guild's monthly guild maintenance
fees.  To maintain a state of war costs 1,000 gold per day, also taken out
with the guild's monthly maintenance charges.
</p><p>The GM may also only commence hostilities with three guilds at a time.
To maintain this state of hostility costs 500 gold pieces per day, which
will be taken out with the guild's monthly maintenance charges.
</p><p>If the two guilds have both declared each other Enemy, a state of
Mutual Warfare exists.  During mutual warfare, all crimes committed
between two warring guilds are outside the lawsys and cannot be reported.
A message is given when the crime is committed reminding both the criminal
and the victim of this fact.  In-non Mutual Warfare, all murder fines will
be capped at 7,000 gold pieces, which will be paid by the guild.
</p><p>If two guilds have both set each other as Ally, a state of Mutual
Alliance exists.  During Mutual Alliance, each guild can see if the other
guild is at war - either having declared war or been declared war against.
This allows Ally guilds to help out their ally as they see fit.
</p><p>Important note: The payment of fines from the guild vault is stored
at the time the crime is committed, but not deducted from the guild vault
until the criminal pleads his case and a fine is calculated.  Because of
this it is possible to see warcosts appear days after you have ended
hostilities, as reports are filed, subpoenas answered, and guildmembers
plead their case to the judge of crimes committed during the period of
hostilities and warfare.
</p><p>To prevent "postal worker syndrome" - a guild member going nuts killing
everyone and racking up enormous fines for the guild, each member has a
per-uptime limit on the number of guild-sponsored crimes they can commit.
This is known as the "warcap", and it is reset every reboot.  The GM sets
the warcap, and can reset it at any time.  Members that have exceeded the
number of warcap crimes against hostile or enemy-ranked guilds will still
be able to commit crimes, however their fine will not be adjusted or paid
for with any guild funds.
</p><p>The following commands are available for the alliance system:
</p><p>In the members room (accessible by every guildmember):
</p><ul><li><span style="font-weight:bold">allies</span> - displays the guild's alliance status.
</li></ul><p>In the guildmaster's room (accessible only by the guildmaster):
</p><ul><li><span style="font-weight:bold">improve &lt;guildname&gt;</span> - improve your ranking of another guild.  If you 
are improving the ranking of a guild that is currently set to Enemy
when there are already three other guilds set to the Hostile ranking,
the Enemy guild will be set to Disliked.
</li><li><span style="font-weight:bold">degrade &lt;guildname&gt;</span> - degrade your ranking of another guild.  Degrading
a guild that is currently ranked "Hostile" will result in an "Enemy"
ranking and the guild will be charged the war charge.
</li><li><span style="font-weight:bold">warcosts</span> - show a printout of the war charges accumulated during this
guild maintenance cycle.
</li><li><span style="font-weight:bold">warcap</span> - show a printout of the current warcap, and the number of 
guild-sponsored crimes each guildmember has committed this uptime.
</li><li><span style="font-weight:bold">warcap reset</span> - reset the sponsored crimes list, allowing each 
guildmember to kill again up to the warcap.
</li><li><span style="font-weight:bold">warcap &lt;#&gt;</span> - set the per-member per-uptime sponsored crime limit.
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
