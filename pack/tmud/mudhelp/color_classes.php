<h2>Color classes (Technical Help)</h2><p>The text that is sent to the players in The Two Towers is structured
into "messages".  Each message has its own type (also known as "class"),
such as "room description", "combat", etc.  Messages can also be nested
within each other, like "exits" description being contained within
"room description".  To make the text more readable, for most of these
message types you can set your own color using the "color" command.
Furthermore, if your client supports MXP (see "<a href="mxp.php">help mxp</a>" for more
details), you may also be able to change the font face and/or size.
</p><p>This help file provides a reference of all available classes so that
you can better understand what the "color" command entries mean.  The list
is presented by category to make it easier to understand; please note,
however, that the "color" command will list the classes in alphabetical
order instead.
</p><h3>1. Descriptions</h3><ul><li><span style="font-weight:bold">room_desc</span> - This is the default color in which the room descriptions 
are displayed.  If no other specific colors are set, this works for
both "glance" and "look" and covers exits, items and NPCs present
in room, weather description, etc.
</li><li><span style="font-weight:bold">room_short</span> - This is the color in which the room "title" will be 
displayed in "glance" command, as well as during fast movement such
as "travelto".  If not set, the display defaults to what is set for
"room_desc".
</li><li><span style="font-weight:bold">exits</span> - This is the color in which the exit names are displayed for 
both "look" and "glance".  When not set, the display defaults to
what is set for "room_desc".  Commas and the word "and" in the exit
description are not covered by this color, unless you have
"full_exit_color" variable set to "on" (see "<a href="sets.php">help sets</a>" or check
"settings" command for more details on this).
</li><li><span style="font-weight:bold">water_exits</span> - Same as "exits", but for water exit names, that is 
directions you can swim to.
</li><li><span style="font-weight:bold">item_desc</span> - This is the color in which all specific descriptions are
displayed.  To be more precise, this applies to "look &lt;something&gt;"
and "glance &lt;something&gt;".
</li><li><span style="font-weight:bold">ercolor</span> - This is the color in which evil player "short descriptions"
will be displayed in room contents (both for "look" and "glance").
If not set, this defaults to what is set for "room_desc".  Note
that the display applies to both Evil Races and Free Race servants
of Sauron.
</li><li><span style="font-weight:bold">frcolor</span> - Similar to the above, but for all other players (non-servant
Free Races).
</li><li><span style="font-weight:bold">npccolor</span> - This is the color in which NPC "short descriptions" will be 
displayed in room contents.  If not set, it defaults to what is set
for "room_desc".
</li><li><span style="font-weight:bold">itemcolor</span> - This is the color in which item (that is, not players and
not NPCs) "short descriptions" will be displayed in room contents.
If not set, it defaults to what is set for "room_desc".
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
<td style="border:0px;white-space:nowrap;" valign="top">Note:</td><td style="border:0px" valign="top"><div style="margin:0px;padding;0px;margin-left:10px;">
frcolor and ercolor also apply to movement messages from other
players, where otherwise "movement" class would be used (see below).

</div></td></tr></table><br/>
<h3>2. Communication</h3><ul><li><span style="font-weight:bold">say</span> - This is the color in which says from other players and NPCs will
be displayed, such as "Joe says: ...".  This also covers soul emotes
(see "<a href="semotes.php">help semotes</a>").
</li><li><span style="font-weight:bold">tell</span> - This is the color in which tells from other players will be 
displayed, such as "Joe tells you: ...".
</li><li><span style="font-weight:bold">tell_outgoing</span> - This is the color in which tells sent by you will be 
displayed, such as "You tell Joe: ...".
</li><li><span style="font-weight:bold">whisper</span> - This is the color in which whispers from other players will 
be displayed, such as "Joe whispers to you: ...".
</li><li><span style="font-weight:bold">emote</span> - This is the color in which emotes performed by other players 
using the "emote" and "emoteat" commands will be displayed.  Note that
this does not cover soul emotes; these use "say" class.
</li><li><span style="font-weight:bold">tellhist_incoming</span> - This is the color in which tells sent by other 
players will be displayed in the "tellhist" command output.
</li><li><span style="font-weight:bold">tellhist_outgoing</span> - This is the color in which tells sent by you will 
be displayed in the "tellhist" command output.
</li><li><span style="font-weight:bold">partysay</span> - This is the color in which messages sent by other players in 
your party using "party say" command will be displayed.
</li><li><span style="font-weight:bold">partyemote</span> - This is the color in which messages sent by other players
in your party using "party emote" command will be displayed.
</li><li><span style="font-weight:bold">plea</span> - This is the color in which messages sent by players via the 
"plea" command will be displayed.
</li><li><span style="font-weight:bold">shout</span> - This is the color in which shouts of other players will be
displayed.
</li></ul><h3>3. Communication - channels</h3><p>You can configure color for each channel (comm) you have access to.
Channels are displayed in a separate section of "color" command.
</p><p>The following channels are available to players:
</p><ul><li><span style="font-weight:bold"><a href="comm.php">comm</a></span> - also known as OOC; available to all players above level 4.
</li><li><span style="font-weight:bold"><a href="newbiecomm.php">newbiecomm</a></span> - available to all players under level 6 and to newbie
helpers.
</li><li><span style="font-weight:bold"><a href="fr.php">fr</a>, <a href="er.php">er</a></span> - available to Free Race / Evil Race+Servants respectively,
for players above level 4.
</li><li><span style="font-weight:bold"><a href="clan.php">clan</a></span> - available to players who are in a clan.
</li><li><span style="font-weight:bold"><a href="guild.php">guild</a></span> - available to players who are in a guild.
</li><li><span style="font-weight:bold"><a href="newbiehelper.php">newbiehelper</a></span> - available to newbie helpers only.
</li><li><span style="font-weight:bold"><a href="ptcomm.php">ptcomm</a></span> - available to playtesters only.
</li><li><span style="font-weight:bold"><a href="gm.php">gm</a></span> - available to guildmasters only.
</li></ul><p>When you set a color to any of these channels, the incoming messages
on that channel will be displayed with this color.  Also, unless you set
"commhist_color" to "off", history for that specific channel will also
be displayed using this color.
</p><h3>4. Combat</h3><ul><li><span style="font-weight:bold">combat</span> - This is the color in which all combat messages will be 
displayed.  This applies to:
<ul><li>hit (or miss) emotes from your bare hands or your weapon.
</li><li>special attacks from professional abilities (like headbutt
or feint).
</li></ul><p>Since most weapon emotes include colors on their own, you will probably
hardly notice the "combat" class color there - only the word "You" and
your enemy's name will be coloured using it.  However, if you change
the font face and/or size for the "combat" class (requires client MXP
support), the whole weapon emote will be displayed with your chosen
font.
</p></li><li><span style="font-weight:bold">lawsys</span> - This is the color for the messages from the courthouse.
Usually they will remind you about your outstanding subpoenas and
fines to pay.  This color will also be used for all lawsys-related
information for your current combat, such as a notification that
you are in a lawless area and an attack on you cannot be reported.
</li><li><span style="font-weight:bold">spar</span> - This is the color for various messages from the sparring system,
such as about players joining and leaving your spar group.
</li><li><span style="font-weight:bold">shape</span> - This is the color used for output of "shape" command.
</li><li><span style="font-weight:bold">consider</span> - This is the color used for output of "consider" command.
</li></ul><h3>5. Events</h3><ul><li><span style="font-weight:bold">weather</span> - This is the color for weather messages, such as about rain
starting, or sun setting.  This applies only for spontaneous "event"
messages, not weather description included in room descriptions.
</li><li><span style="font-weight:bold">auction</span> - This is the color for tells (and listings) from the auction
NPCs, such as "Laryn tells you: an item is now on sale...".
</li><li><span style="font-weight:bold">announce</span> - This is the color for announces of your friends logging in;
this will work only if you have "announce_friends" variable set.
</li><li><span style="font-weight:bold">hints</span> - This is the color for random gameplay hints that new players 
receive.
</li><li><span style="font-weight:bold">movement</span> - This is the color in which movement messages from other
players and NPCs are displayed, such as "Joe enters." or "Bob leaves
west.".  Note that for players, "movement" class is overridden by
"frcolor"/"ercolor" if you have them set.
</li><li><span style="font-weight:bold">system</span> - This is the color used for system broadcasts, sent by the MUD 
driver automatically (such as on beginning of a reboot) or manually by
the Administrators.
</li><li><span style="font-weight:bold">shutdown</span> - This is the color used for the reboot countdown messages.
</li></ul><h3>6. Miscellaneous</h3><ul><li><span style="font-weight:bold">songs</span> - This is the color in which text of your songs will be displayed
as you perform them.  Note that this is the only color class that
affects text seen both by you and other people (those who listen to
your songs).  Because of that, you cannot use MXP for this setting.
</li><li><span style="font-weight:bold">menutitle, menubody, menuprompt</span> - They are colors used for respectively
header, body and footer of menus created with the T2T generic menu
system.  This system isn't widely used so far for regular players:
just in the "settings" and "removetag" commands, so there is little
use for these settings.  Menus are useful for guildmasters, though,
since some features of the GMEdit system use them.
</li></ul><h3>7. "who" command display</h3><p>The "who" command is the most customizable command on The Two Towers.
With it, you can set different colors for different players, to be able to
quickly find friends, enemies, and others amongst the players who are
currently online.  There are several classes you can set here:
</p><ul><li><span style="font-weight:bold">whoclanmate</span> - For players in your clan.
</li><li><span style="font-weight:bold">whoguild</span> - For players in your guild.
</li><li><span style="font-weight:bold">whofriend</span> - For people on your "friend" list.
</li><li><span style="font-weight:bold">whoenemy</span> - For people on your "enemy" list.
</li><li><span style="font-weight:bold">whonewbie</span> - For new T2T players.
</li><li><span style="font-weight:bold">whofree</span> - For non-servant Free Race players.
</li><li><span style="font-weight:bold">whoevil</span> - For Evil Race players and Free Race servants of Sauron.
</li><li><span style="font-weight:bold">whoneighbor</span> - For people who have MUD mail address at the same town 
as you.
</li><li><span style="font-weight:bold">whoguildally</span> - For people who are in guilds allied with your guild
(if their "guildinfo" is set to public).
</li><li><span style="font-weight:bold">whoguildenemy</span> - For people who are in guilds hostile to your guild
(if their "guildinfo" is set to public).
</li></ul><p>For each of the classes you can set a different color so that the
"who" output is clear.
</p><p>The priority of the classes is as follows:
</p><p>friend &gt; enemy &gt; clan &gt; guild &gt; newbie &gt; guildally &gt; guildenemy &gt;
neighbor &gt; fr &gt; er
</p><p>That is, if someone is both a newbie and on your friend list, their
name will be displayed using the "whofriend" class rather than "whonewbie".
