<h2>Cards (Objects Help)</h2><p>In various quest sites in Arda you may find decks of cards.  They
are usually not the main quest reward, but a side one obtainable with
less effort.  The purpose of these decks is to allow players play any
card game they would want to.
</p><p>Once you have a deck of cards in your hands, you may use the
following commands:
</p><ul><li><span style="font-weight:bold">cmds</span> - Print detailed command help.
</li><li><span style="font-weight:bold">random</span> - Pull a random card from the deck, show it, and stick it
back into the deck.
</li><li><span style="font-weight:bold">prepare deck</span> - Prepares the deck of cards for a game.
</li><li><span style="font-weight:bold">view [deal|discard]</span> - Turn deal/discard viewing on or off.  Typing
"view" with no arguments will show you the current value of this flag.
</li><li><span style="font-weight:bold">flip [table]</span> - Used with no arguments to flip a card in your hand.  
Flip "table" to flip a card on the table.
</li><li><span style="font-weight:bold">deal &lt;player&gt; [#] [up|down]</span> - Deal a number of cards (default 1) to a
player and deal it face up or face down (so that even they cannot look
at it).  Defaults to up.  Deal to "table" to deal a card onto the table.
</li><li><span style="font-weight:bold">shuffle [all|table]</span> - Shuffles the discard pile or all of the cards
(including cards currently in players' hands).  Defaults to just the
discard pile.
</li><li><span style="font-weight:bold">dealer &lt;person&gt;</span> - Make a new person the dealer.
</li><li><span style="font-weight:bold">gather cards</span> - Gathers all the cards back up.
</li></ul><p>When you have been dealt a hand of cards, you may:
</p><ul><li><span style="font-weight:bold">show &lt;card|hand&gt;</span> - Shows a card or your hand to the room.
</li><li><span style="font-weight:bold">discard &lt;card|hand&gt;</span> - Discards the entire hand, or just one card, 
to be picked.
</li><li><span style="font-weight:bold">pass &lt;person&gt;</span> - Pass a card to another player.
</li><li><span style="font-weight:bold">place &lt;card|hand&gt;</span> - Place a card or your entire hand on the table.
</li><li><span style="font-weight:bold">pickup card</span> - Take a card from the table.
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
