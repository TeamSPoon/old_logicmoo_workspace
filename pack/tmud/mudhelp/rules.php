<h2>Rules (Rules and Policies)</h2><p>These are the rules of conduct for The Two Towers.  By playing The
Two Towers, you are implying agreement to follow these rules regardless
of whether you agree with them or not.  Continuously ignoring and/or
breaking these rules is taken as indication that you no longer wish to
continue playing The Two Towers and we shall act accordingly.
</p><p>In general there are three guidelines for acceptable behavior:
</p><ul><li>Respect the MUD as a Community.
</li><li>Respect the MUD as a computer system.
</li><li>Respect the rights of other people to use and enjoy the system.
</li></ul><p>We expect players to follow the spirit of the rules, not just the
letter of the law.  If you think you have found a way around the letter,
you can be fairly certain you are violating the spirit and will be
treated no less harshly.
</p><ol style="list-style-type:decimal"><li><span style="font-weight:bold"><a href="profanity.php">Profanity</a></span> - Keep foul/abusive language out of public forums.  See
'<a href="comm_policy.php">help comm policy</a>' for our rules for public channels, and be aware
that we consider guild comms to be public channels.
</li><li><span style="font-weight:bold"><a href="harassment.php">Harassment</a></span> - Try to keep personal attacks and arguments out of
public forums. We will become involved if such things get out of
hand.  We may also get involved in private instances of harassment.
See '<a href="harassment.php">help harassment</a>' for more detailed guidelines.
</li><li><span style="font-weight:bold"><a href="spam.php">Spam</a></span> - Do not spam commands, spam on channels, or spam in any
other way, especially during combat.
</li><li><span style="font-weight:bold"><a href="multiplaying.php">Multiplaying</a></span> - Your characters cannot be online at the same time
or aid each other in any way.  The same character cannot be shared by
more than one player.
</li><li><span style="font-weight:bold"><a href="bug_abuse.php">Bug Abuse</a></span> - If you find a bug, or think you might have found a
bug, report it.  The exploitation of a bug will not be tolerated.
</li><li><span style="font-weight:bold"><a href="triggers.php">Triggers</a></span> - Client triggers are subject to the rules in '<a href="botting.php">help botting</a>',
'<a href="triggers.php">help triggers</a>' and '<a href="client_abuse.php">help client abuse</a>'. Also, any triggers used to
simulate the hunt ability, or artificially inflate your character age,
are not allowed.
</li><li><span style="font-weight:bold"><a href="botting.php">Botting</a></span> - Do not use scripts to control your character while you
are not at the keyboard.
</li><li><span style="font-weight:bold"><a href="killstealing.php">KillStealing</a></span> - Repeated/consistent stealing of another player's
kills is considered harassment and will be handled as such; other
than that, killstealing is not against the rules.
</li><li><span style="font-weight:bold"><a href="playerkilling.php">PlayerKilling</a></span> - We allow limited player killing, subject to the
game's internal law system.  However we do not allow the killing of
new players.  Please note that since the implementation of the
Blessing of Mandos, the Administration no longer recognizes
levelbashing or spreekilling as law-reportable offenses.  See '<a href="levelbashing.php">help levelbashing</a>' and '<a href="spreekilling.php">help spreekilling</a>' for more information.
</li><li><span style="font-weight:bold"><a href="playerstealing.php">PlayerStealing</a></span> - We allow players to steal from other players,
subject to the games internal law system.  However, repeated
stealing from the same player as a form of harassment is not
allowed.
</li><li><span style="font-weight:bold"><a href="questinfo.php">QuestInfo</a></span> - Do not give out quest information using public lines of 
communication such as comms and boards.
</li><li><span style="font-weight:bold"><a href="roleplaying.php">Roleplaying</a></span> - The Two Towers is a roleplaying MUD, and some level
of roleplaying is required when using certain features.  In particular,
check '<a href="names.php">help names</a>' and '<a href="titles.php">help titles</a>'.
</li><li><span style="font-weight:bold"><a href="advertising.php">Advertising</a></span> - Do not advertise other online games or commercial 
products on The Two Towers.  Do not use our MUD to organize playing
other games.
</li><li><span style="font-weight:bold"><a href="troublemaking.php">Troublemaking</a></span> - General trouble making such as public insults,
flames, taunts, excessive complaining, and similar disruptive
behavior is not tolerated.
</li><li><span style="font-weight:bold"><a href="proxies.php">Proxies</a></span> - Connection via proxy servers/accounts is forbidden, 
unless express permission has been granted by the Administration.
</li></ol><p>Please refer to the specific help file of any of the above rules for
more detailed information.
</p><p>Violation of any of these rules may result in various punishments,
from a formal warning to deletion of all of your characters and banishment
from the game.  See '<a href="punishments.php">help punishments</a>' for further details on what may
happen to rule breakers.
</p><p>If you observe a violation of our rules, please use the 'law' command
to bring it to our attention.
</p><p>For other rule-related topics that do not refer directly to things
we disallow (such as the process of bug reporting), see '<a href="policies.php">help policies</a>'.
