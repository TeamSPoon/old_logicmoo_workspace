<h2>Harassment (Rules and Policies)</h2><p>Harassment is not tolerated on The Two Towers, including any and all
forms of sexual harassment.  We will not allow harassment on the basis of
gender, ideas, nationality, sexual orientation, religious beliefs, or any
other manner which interferes with the ability of another person to enjoy
the game (such as "levelbashing", "killstealing", or the like).
</p><p>Generally, if someone asks you to stop and you don't, then it can
be considered harassment unless it is a normal part of the game such as
solitary playerkilling.
</p><p>Disagreements and arguments can sometimes escalate into harassment.
If this happens, the "ignore" command is provided to help you deal with
this situation, so please use it as your first line of defense.
</p><p>While no one is required to be nice to others, consistently insulting
and/or baiting an individual or group qualifies as harassment and will be
handled as such.  This ties in with #14 (Troublemaking clause) in "<a href="rules.php">help rules</a>".  Repeatedly taunting your fellow players until they react angrily,
for your own entertainment, is not acceptable and does not foster the sort
of gameplay environment that we want this MUD to have.
</p><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
<td style="border:0px;white-space:nowrap;" valign="top">Note:</td><td style="border:0px" valign="top"><div style="margin:0px;padding;0px;margin-left:10px;">
This does NOT mean that you should come running to the ainur every 
time someone is mean to you.  This is about REPEATED, CONSISTENT
insults and abuse.

</div></td></tr></table><br/>
<p>In addition, any form of Real-Life threats will be treated as
harassment and punished accordingly, and if necessary will be reported
to appropriate law enforcement agencies.
</p><p>If you find yourself a victim of harassment, you can use the "harass"
command to report the incident to the Administration for their review, or
use the "law" command to file a law report.  Note that "harass" command
files a law report automatically, so there is no need to do both.
</p><p>For the avoidance of doubt, harassment in the context of being a
victim of multiple attempts is 5 or more attempts by an individual on a
victim's characters within the space of 1 real life month.  As with
spreekilling we will take into account factors such as mutual hostilities
in determining guilt.
