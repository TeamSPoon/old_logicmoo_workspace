<h2>Overthrow (Social command)</h2><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
<td style="border:0px" valign="top"><span style="font-weight:bold">Usage:</span></td>
<td style="border:0px" valign="top">
<pre style="margin:0px;padding;0px;margin-left:10px">overthrow &lt;current GM&gt; for &lt;proposed new GM&gt;<br/>overthrow -status<br/>overthrow -reconsider</pre>
</td></tr></table>
<p>The overthrow command is a tool for guildmembers to declare their
desire to replace a current GM with another member of their guild.  When
using the command, the malcontent with the current leadership is logged,
as well as the suggested new Guildmaster.  Once two thirds of a guild's
active members have indicated that they wish for a new Guildmaster, the
sitting GM is deposed and replaced with the member who got most votes.
Guildmembers can get an indication of the popularity of the leadership
inside their guildhall, with the 'leadership' command in the main guild
room.  An individual vote will automatically expire after 15 RL days.
You are defined as an active member if you have aged 4 hours in the last
30 days.
</p><p>You may only use the overthrow command if you have been a member of
the guild for 2 weeks or more.  If you are expelled after being a member
for 2 weeks or more, you may use the overthrow command for up to 2 weeks
afterwards.  Only members of 2 weeks or more or eligible recently-expelled
players may be considered as replacements for the current Guildmaster.
Note that if you leave and rejoin your guild within 2 weeks, you will
retain your previous join date.
</p><p>Your current overthrow vote can be viewed with the 'overthrow -status'
command and withdrawn with 'overthrow -reconsider'.
</p><p>Be advised that the Administration takes a dim view of hostile
takeovers that do not have a guild's best interest in mind.  For example,
overthrows that are followed by a mass expulsion of members, guild vault
draining, or any other form of guild griefing can expect to be scrutinized
and overturned.
