<h2>Mxp (Technical Help)</h2><p style="font-style: italic">Q: What is MXP?
</p><div style="margin-left: 20px"><p>MXP is the Mud eXtension Protocol that has originally been designed
and developed for Zmud MUD client, and later evolved into an open
specification usable by other clients.
</p><p>The most popular clients that support it currently are: Zmud, Cmud
and Mushclient.  The Two Towers web/facebook client also supports
MXP and has it enabled by default.
</p></div><p style="font-style: italic">Q: How can I use MXP?
</p><div style="margin-left: 20px"><p>To use MXP, you must first enable it in your client.
</p><p>If you use The Two Towers web/facebook client, MXP will be already
enabled - you do not have to configure anything.
</p><p>If you use the Zmud client version 6.15 and over, MXP support should
be already on, unless you have turned it off.  All you should need
to do is type "set mxp on" to enable this.  If you have disabled MXP,
turn it back on in your client from the "View" menu, "Preferences",
"General" and then scroll way down to the MXP option.
</p><p>If you use Mushclient version 3.10 or above, MXP is off by default.
You must use the "Game" menu, "Configure" option, and then select
MXP/Pueblo from the dropdown menu.  Where it says "Use MXP/Pueblo",
set to "Always On".  You should also uncheck the "Use custom
link colour" option, to allow the MUD to colorize the hyperlinks
depending on the message class they are part of.
</p><p>If you use any version of Cmud, MXP should be on by default.  You
should just have to type in "set mxp on" to get clickable links.
If this does not work, use the Options menu, select "General", pick
"MXP" and enable MXP to see clickable links.
</p><p>If you use Cmud 3.0 and above (currently in beta), you will also
have to enable the "Force remote MXP" option.
</p><p>If you use any other MXP-supporting client, check its documentation
on how to turn on MXP.  You should also type: "set mxp on" while
connected to The Two Towers.
</p><p>Once that is set, MXP should be working all by itself once you connect
to The Two Towers again.  The additional login message you will get
will look like this:
</p><pre>    MXP-supporting terminal detected; negotiating MXP...
    (If you can see a 'VERSION' HTML tag below, your client MXP support is turned off;
     press ENTER to continue in that case);
    MXP enabled.
</pre><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
<td style="border:0px;white-space:nowrap;" valign="top">Note:</td><td style="border:0px" valign="top"><div style="margin:0px;padding;0px;margin-left:10px;">
You should also make sure that your "extended color" is turned
on if you plan to use MXP color for MUD messages; you can set
this by typing "color extended_color on".

</div></td></tr></table><br/>
</div><p style="font-style: italic">Q: What does MXP change in the MUD's output?
</p><div style="margin-left: 20px"><p>The Two Towers MUD supports a subset of the protocol, which allows
us to do the following:
</p><ul><li>The MUD output can be now displayed with all range of HTML colors,
rather than being limited to ANSI's 16 colors.  Zmud, Cmud and our
web client can also change font face and font size.
Use the "color" command to change any message class to use any
color/font you want!
</li><li>Some handy hyperlinks are provided for parts of the MUD
output, for those who prefer to play games with mouse rather
than keyboard.  These include:
<ul><li><span style="font-weight:bold">land and water exits</span> - clicking on the exit links will move you
around.  This includes "go" for land exits, "swim" for water
exits and "launch" / "land" for when you are in a boat.
Right-clicking on an exit link will also allow you to "look"
through that exit.
</li><li><span style="font-weight:bold">objects</span> - Objects listed in room's contents will appear as 
hyperlinks; right-clicking on the link brings up a popup menu
for some command shortcuts.  The popup menu is different for
different types of objects (NPCs, locked chests, items, etc.).
This category also includes links on travelto destinations
within signpost descriptions.
</li><li><span style="font-weight:bold">inventory</span> - All objects in your direct inventory will be 
hyperlinks, allowing you to look at them.  A popup menu for
each item will let you manipulate the item.
</li><li><span style="font-weight:bold">doors</span> - A popup menu is available on hyperlink for each door 
listed in room's detailed description, allowing you to quickly
manipulate it.
</li><li><span style="font-weight:bold">board posts</span> - When you look at a board (or at a gossiper NPC),
each post will be a hyperlink so that you can read it with a
single click.
</li><li><span style="font-weight:bold">--More-</span> - prompt - You will be able to click on the prompt to
see next page of text, if you prefer to use mouse instead of
keyboard.
</li><li><span style="font-weight:bold">help files</span> - When viewing help files, any reference to another
help file (such as in the 'See also' list) will be a hyperlink.
References to web pages in help files (such as the link to our
facebook application) will work as hyperlinks as well.
</li></ul></li><li>HP and EP graphical gauges are available in Zmud, Cmud and the
web client.  They usually appear above the main MUD window; in
the web client they are on the scroll image below.
</li></ul><p>If you do not want/like some of these features, they can be turned
on and off individually.  You can do it either with the "set" command
(see "<a href="sets.php">help sets</a>") or via the "settings" command menu.
</p></div><p style="font-style: italic">Q: Something does not work with MXP - where should I report that?
</p><div style="margin-left: 20px"><p>Unfortunately, though clients claim that they support MXP, not every
of them supports in fully and there are some known bugs in the
implementation.  The problems we know of are:
</p><p>Zmud-specific:
</p><ul><li>Setting a custom (MXP/HTML) color to any message class will cause
all standard MUD colorcodes to be ignored within that message.
So eg. if you set your comm color to "skyblue", you will be unable
to see the colorcodes sent by other players on the comm.
</li></ul><p>Cmud-specific:
</p><ul><li>There are some problems with using MXP/HTML background colors with
standard (ANSI) foreground colors, MUD colorcodes included.  The
background may disappear where it shouldn't.
</li></ul><p>Zmud/Cmud:
</p><ul><li>Increasing font size does not work too well with multi-line messages
(the lines overlap).
</li><li>Changing font size or face for a message class that contains
hyperlinks (room_desc, item_desc, exits, water_exits) may create
the "hotspot" for the link to be active in wrong place, as if
the font was never changed in first place.
</li></ul><p>Mushclient:
</p><ul><li>HP/EP gauges are not supported.
</li><li>Changing font face and font size is not supported.
</li><li>Hyperlink "expiration" is not supported.  That is, if you move to
next room, the hyperlinks in the previous room will still be active.
</li></ul><p>If you find any problem not mentioned above, report it normally using
the "bug" command - we will try to investigate the issue!
</p></div><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
