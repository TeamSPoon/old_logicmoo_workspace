<h2>Guild membership (Guild Help)</h2><p>For thematic purposes, a guild may restrict their membership to
players of certain races, professions, and even alignments.  To aid
the guildmasters in tracking this information, the 'membership' command
is available in the GM room.  Violations are clearly marked.
</p><p>For each of the three categories, the 'membership' command will
display the percentage of active guildmembers fitting that category
(two composite racial groups of 'elf' and 'man' are provided for quick
reference).  Activity is defined as 4 or more hours of aging over the
previous 30 days.  The minimum or maximum percentage of each category
is set by the guildmaster when the guild is formed, with the thematic
approval of Valinor.
</p><p>The addition of a new guildmember will fail if the that person would
cause the guild to violate any of its requirements.
</p><p>Sample output from the membership command:
