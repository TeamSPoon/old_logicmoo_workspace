<h2>Abilities (Command Help)</h2><p>Abilities on The Two Towers are available to anyone and everyone, but
certain professions are better at certain abilities, and others are good
at other abilities.  If you are attempting to use an ability that is not
used regularly by your profession, you probably won't be very successful.
You always have a chance, but it is a slim one.  Here are lists of the
major abilities available to each profession:
</p><ol style="list-style-type:decimal"><li>Archer
<ul><li><span style="font-weight:bold"><a href="kickback.php">kickback</a></span> - Kick your opponent for some damage.
</li><li><span style="font-weight:bold"><a href="multinock.php">multinock</a></span> - Fire two arrows at once!
</li><li><span style="font-weight:bold"><a href="fletch.php">fletch</a></span> - Create new arrows from materials available in forests.
</li><li><span style="font-weight:bold"><a href="salvage.php">salvage</a></span> - Gather back your arrows after a fight.
</li><li><span style="font-weight:bold"><a href="restring.php">restring</a></span> - Repair your broken bow.
</li><li><span style="font-weight:bold"><a href="tie.php">tie</a></span> - Tie a bunch of arrows into a bundle for easier handling.
</li></ul></li><li>Assassin
<ul><li><span style="font-weight:bold"><a href="hunt.php">hunt</a></span> - Follow your target automatically. (*)
</li><li><span style="font-weight:bold"><a href="camouflage.php">camouflage</a></span> - Become invisible to your potential victims. (*)
</li><li><span style="font-weight:bold"><a href="backstab.php">backstab</a></span> - Initiate combat by surprise with a lot of damage.
</li><li><span style="font-weight:bold"><a href="disguise.php">disguise</a></span> - Change your appearance to fool your enemies.
</li></ul></li><li>Warrior
<ul><li><span style="font-weight:bold"><a href="headbutt.php">headbutt</a></span> - Bash your head into the enemy for a lot of damage.
</li><li><span style="font-weight:bold"><a href="bash.php">bash</a></span> - Break open doors and locked chests.
</li><li><span style="font-weight:bold"><a href="compare.php">compare</a></span> - Gauge the difference between two items, eg. weapons.
</li><li><span style="font-weight:bold"><a href="condition.php">condition</a></span> - Tell how badly damaged a piece of equipment is.
</li><li><span style="font-weight:bold"><a href="repair.php">repair</a></span> - Improve the condition of an item.
</li></ul></li><li>Rider
<ul><li><span style="font-weight:bold"><a href="tame.php">tame</a></span> - Make a wild mount your own.
</li><li><span style="font-weight:bold"><a href="mount.php">mount</a></span> - A group of various commands to interact with your mount.
</li><li><span style="font-weight:bold"><a href="trample.php">trample</a></span> - Crush your enemies under your mount's hooves.
</li><li><span style="font-weight:bold"><a href="unseat.php">unseat</a></span> - Remove an enemy from their mount.
</li><li><span style="font-weight:bold"><a href="warcry.php">warcry</a></span> - Inspire your party to fight better.
</li></ul></li><li>Thief
<ul><li><span style="font-weight:bold"><a href="pick.php">pick</a></span> - Open locks that were not expected to be opened.
</li><li><span style="font-weight:bold"><a href="steal.php">steal</a></span> - Claim ownership of things from someone else.
</li><li><span style="font-weight:bold"><a href="hide.php">hide</a></span> - Make potential victims unaware of your presence.
</li><li><span style="font-weight:bold"><a href="sneak.php">sneak</a></span> - Move into another room without notifying anybody about it.
</li><li><span style="font-weight:bold"><a href="appraise.php">appraise</a></span> - Approximate the value of an item, or someone's pouch size.
</li></ul></li><li>Ranger (Free Races only)
<ul><li><span style="font-weight:bold"><a href="eaglesight.php">eaglesight</a></span> - Gain the ability to see in the dark.
</li><li><span style="font-weight:bold"><a href="heal.php">heal</a></span> - Use healing herbs to restore your, or someone else's, health.
</li><li><span style="font-weight:bold"><a href="feint.php">feint</a></span> - A combat action for dealing extra damage to your opponent.
</li><li><span style="font-weight:bold"><a href="trap.php">trap</a></span> - Set a trap to make it harder for your opponent to fight you.
</li><li><span style="font-weight:bold"><a href="track.php">track</a></span> - Follow someone's tracks, up to their present location.
</li><li><span style="font-weight:bold"><a href="map.php">map</a></span> - Rangers can see larger maps than other professions.
</li></ul></li><li>Wizard (Free Races only)
<ul><li><span style="font-weight:bold"><a href="cast.php">cast</a></span> - Use one of your spells.
</li><li><span style="font-weight:bold"><a href="counter.php">counter</a></span> - Stop someone else's spell in progress.
</li><li><span style="font-weight:bold"><a href="enchant.php">enchant</a></span> - Transfer a spell to an object for future use.
</li><li><span style="font-weight:bold"><a href="find.php">find</a></span> - Attempt to figure the exact location of another player.
</li></ul></li><li>Sniffer (Evil Races only)
<ul><li><span style="font-weight:bold"><a href="shiv.php">shiv</a></span> - A combat action for dealing extra damage.
</li><li><span style="font-weight:bold"><a href="attune.php">attune</a></span> - Gain the ability to see in the dark.
</li><li><span style="font-weight:bold"><a href="prowl.php">prowl</a></span> - Become invisible to your potential victims. (*)
</li><li><span style="font-weight:bold"><a href="track.php">track</a></span> - Follow someone's tracks, up to their present location.
</li><li><span style="font-weight:bold"><a href="sniff.php">sniff</a></span> - Attempt to determine how far away another player is from you.
</li></ul></li><li>Necromancer (Evil Races only)
<ul><li><span style="font-weight:bold"><a href="conjure.php">conjure</a></span> - Use one of your spells.
</li><li><span style="font-weight:bold"><a href="leech.php">leech</a></span> - A spell transferring enemy's life force to you.
</li><li><span style="font-weight:bold"><a href="sacrifice.php">sacrifice</a></span> - A spell transferring your life force to someone else.
</li><li><span style="font-weight:bold"><a href="imbue.php">imbue</a></span> - Repair (refuel) a magical light- or dark-emitting object.
</li><li><span style="font-weight:bold"><a href="sense.php">sense</a></span> - Attempt to figure the exact location of another player.
</li></ul></li></ol><p>(*) - Restricted abilities which cannot be used by players of another
profession.
