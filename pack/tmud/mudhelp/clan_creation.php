<h2>Clan creation (Guild Help)</h2><p>Here is a step by step guideline to creating a clan, which is the
first step in creating a guild.
</p><ol style="list-style-type:decimal"><li>An idea is born, which will enhance Arda by its being implemented.
This idea is a new clan, which you wish to propose.  Accordingly,
it is thematic and true to Tolkien's vision of Arda.
</li><li>The leader, hereafter called the clanmaster, heads to the clan creation
room in Bree, Khemnahr or Barad-d&ucirc;r and registers to create a new clan.
This costs 1,000 gold.
</li><li>Players who believe in this leader's vision can go to the clan room 
and pledge to join the leader's clan.  The player must first leave
any guild or clan to which they already belong.  The clanmaster adds
the pledges to the clan, or rejects their pledge.
</li><li>In the first five days the clan must recruit its founding five members.
If the clan does not meet the five member minimum it is disbanded.  Once
the clan meets the minimum number of founding members, the clan can be
proposed to Valinor.
</li><li>The clanmaster must go to the clan room and 'upgrade' the forming clan
to a proposed clan.  The clan proposal will go before the Guilds Liaison
for approval.  From now on the clan must maintain the minimum of five
members or risk deletion.  Any clan member that does not log in for 15
days will be automatically removed from the clan, regardless of
hibernation status.
</li><li>If the Guilds Liaison denies the application, the clanmaster will be 
informed and the clan deleted.  This should only happen if the clan
proposal is obscene, offensive, or completely unthematic.
</li><li>When the clan proposal has been approved, the clanmaster may upgrade
to fully privileged clan status.  The upgrade must be done in the clan
room and will cost 10,000 gold.
</li><li>A fully privileged clan receives its own comm line, usable with the 
'clan' command and the '$' symbol.  See '<a href="clan.php">help clan</a>' for more information
about this.  The clan also appears now on the clan list in the clan
room.  The clanmaster may purchase a comm history feature for 5,000
gold.
</li><li>The clanmaster can at any time use the 'appoint' command in the clan 
creation area to pass leadership to the successor of their choosing.
If the clanmaster is nuked, suicides, or does not log on for 15 days
the clan will be deleted.
</li></ol><p>From here, steps to create a guild continues in '<a href="guild_new.php">help guild new</a>' and
'<a href="clan_stages.php">help clan stages</a>'.
