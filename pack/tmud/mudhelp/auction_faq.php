<h2>Auction faq (Services Help)</h2><p style="font-style: italic">Q: When can I use the auction?
</p><div style="margin-left: 20px"><p>You must be level 3 or 24 hours old.
</p></div><p style="font-style: italic">Q: Where are the auction shops located?
</p><div style="margin-left: 20px"><p>Laryn is located in the town of Bree.  Alexa is located at an outpost
near Calenhad, in the White Mountains between Edoras and Minas Tirith.
Alazif can be found in Tul-Tarzhayan in Far Harad.  Eamon is at Kadar,
in the Harondor region.
</p></div><p style="font-style: italic">Q: How can I see what is being auctioned?
</p><div style="margin-left: 20px"><p>You can simply type 'list' at the auction house, or if you cannot be
bothered to go there, you can 'tell &lt;auctioneer&gt; list'.  If, however,
you want to examine an item closely, you will have to travel to the
auction house and 'handle' the item just like in regular shop.
</p></div><p style="font-style: italic">Q: How can I know about new auctions without constantly checking the list?
</p><div style="margin-left: 20px"><p>You can ask the auctioneer to send you informing tells.  To do so you
may either type 'announce on' in the auction house or you may 'tell
&lt;auctioneer&gt; announce on'.  To disable these tells again, just 'tell
&lt;auctioneer&gt; announce off'.
</p></div><p style="font-style: italic">Q: How do I auction something?
</p><div style="margin-left: 20px"><p>You go to the auctioneer, and do 'auction &lt;item&gt; min &lt;minimum bid&gt;'.
Optionally, you can append a tag to your item to include additional
information, like: 'auction sword min 100 tag new'.  See '<a href="auction_tags.php">help auction tags</a>' for commonly used tag abbreviations.  The minimum
starting price for an auction is 50 gold.
</p></div><p style="font-style: italic">Q: How do I auction a wagon or a mount?
</p><div style="margin-left: 20px"><p>You lead your wagon or mount as close to the auctioneer as it is
possible, usually leaving it on a street or courtyard just outside.
Then go to the auctioneer and 'auction &lt;wagon/mount&gt; min
&lt;minimum bid&gt;' as usual.
</p></div><p style="font-style: italic">Q: How do I auction many items at once?
</p><div style="margin-left: 20px"><p>You go to the auctioneer, and do 'auction package'.  This will prompt
you to specify all the items you want auctioned and later ask for
the minimum bid price.  You can also append a tag to your package,
as with single-item auctions: 'auction package tag new'.  The minimum
starting price for an auction package is 150 gold.
</p></div><p style="font-style: italic">Q: How much are the auctioneer fees?
</p><div style="margin-left: 20px"><p>To auction an item, you need to pay 5% of your initial starting bid
as an additional fee.  There is also a withdrawal fee, should you
decide to cancel the auction: 10% of highest bid + 2% per every hour
item was on sale.
</p></div><p style="font-style: italic">Q: How do I bid on something?
</p><div style="margin-left: 20px"><p>You can either type 'bid &lt;lot number&gt; &lt;your bid&gt;' at the auction house
or you can 'tell &lt;auctioneer&gt; bid &lt;lot number&gt; &lt;your bid&gt;' from
anywhere in Arda.  Each new bid after the initial bid on an item must
be at least 10 gold higher than the current standing bid.
</p></div><p style="font-style: italic">Q: How do I pick up the item I won on auction / withdraw an item from 
my auction?
</p><div style="margin-left: 20px"><p>You must travel to any of the auction houses and 'pickup lot &lt;number&gt;'.
For picking up won items, you have 10 minutes to do so; if you do not
pick the item, it will go back on sale.
</p></div><p style="font-style: italic">Q: What happens if I don't pick up an item on time?
</p><div style="margin-left: 20px"><p>You have 10 minutes to pick up the item. If you don't, said item will
be reauctioned.  After you fail to pick up an item for a second time,
the auctioneer will not accept any bids from you for 2 weeks unless
you pay off your penalties at any auction house for a set price.
</p></div><p style="font-style: italic">Q: How can I pay off auction penalties?
</p><div style="margin-left: 20px"><p>If the auctioneer blocked you from using his/her service due to
failing to pick up an item twice, you can make them change their mind
with an appropriate fee.  To see how much will it cost you, visit any
auctioneer and type 'check penalty'.  If you have enough gold, you
may then 'payoff penalty'.
</p></div><p style="font-style: italic">Q: How much gold will the auctioneer hold for me?
</p><div style="margin-left: 20px"><p>20,000 gold.  Any proceeds exceeding 20000 gold will be kept by the
auctioneer, so be sure to 'pickup gold' frequently!  Auctions may end
with a bid higher than 20,000 gold, but the seller will not receive
full compensation.
</p></div><p style="font-style: italic">Q: What happens if I log off?
</p><div style="margin-left: 20px"><p>If you log off or die while having an item on auction, the auctioneer
will sell the item to shop, and you will receive shop price minus
auctioneer fees.
</p></div><p style="font-style: italic">Q: How do I post a request on the auction ledger?
</p><div style="margin-left: 20px"><p>You go to auctioneer, and do 'request equipment'.  Then, you enter
your request.
</p></div><p style="font-style: italic">Q: How do I make the auctioneer notify me about new requests?
</p><div style="margin-left: 20px"><p>You simply 'tell &lt;auctioneer&gt; requests on'.  When you no longer wish
to get request notifications, 'tell &lt;auctioneer&gt; requests off'.
</p></div><p style="font-style: italic">Q: How do I change color of tells the auctioneer sends me?
</p><div style="margin-left: 20px"><p>Use the 'color' command and set color for 'auction' messages.
