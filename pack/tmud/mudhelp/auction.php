<h2>Auction (Services Help)</h2><p>The auction is a way for players to sell their goods to other players
through a controlled environment.  It allows individuals to place items
for sale and then others may choose to handle these items and bid on them.
</p><p>Currently, there are four auction houses:
</p><ol style="list-style-type:decimal"><li><span style="font-weight:bold">In Bree</span> - Run by Laryn, this is the auction house for low-level 
Free Race players.
</li><li><span style="font-weight:bold">In Calenhad in the White Mountains near Minas Tirith</span> - Run by Alexa, 
this is the auction house for high-level Free Race players.
</li><li><span style="font-weight:bold">In Tul-Tarzhayan</span> - Run by Alazif, this is the auction house for 
low-level Evil Race players.
</li><li><span style="font-weight:bold">In Kadar</span> - Run by Eamon, this is the auction house for high-level 
Evil Race players.
</li></ol><p>The auction houses are linked: when you put an item on auction in one
of them, the buyer can pick it in any of the four.  The high-level vs
low-level distinction is for bidding on items and picking them up.  This
is made to encourage the highlevels to travel to farther places rather than
stick with just the 'newbie' auction house.
</p><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
<td style="border:0px;white-space:nowrap;" valign="top">Note:</td><td style="border:0px" valign="top"><div style="margin:0px;padding;0px;margin-left:10px;">
To use any (even the low-level) auction house, you must either be 
level 3 or have 1 day of character age.

</div></td></tr></table><br/>
<p>The following commands are available when you are at an auction house:
</p><ul><li><span style="font-weight:bold">auction &lt;item&gt; min &lt;minimum bid&gt; tag &lt;additional information&gt;</span> - Puts an
item on auction.
</li><li><span style="font-weight:bold">pickup &lt;lot #&gt;</span> - Picks up an item you win bid for, or withdraws your
own auction.
</li><li><span style="font-weight:bold">pickup gold</span> - Claim gold revenue from your auctions.
</li><li><span style="font-weight:bold">request equipment</span> - Write a note to other players that you need a 
specific piece of equipment.
</li><li><span style="font-weight:bold">erase request (or request cancel)</span> - Remove such note.
</li><li><span style="font-weight:bold">check penalty</span> - If you failed to pick up a won item twice, this will
display how much you have to pay to be able to use the auction again.
</li><li><span style="font-weight:bold">payoff penalty</span> - To pay off the penalty described above.
</li></ul><p>The following commands are available either directly at the auction
house, or remotely through tells to auctioneers (for example: "tell laryn
list"):
</p><ul><li><span style="font-weight:bold">list</span> - To see what items are currently on sale.
</li><li><span style="font-weight:bold">list &lt;name&gt;</span> - To see what items of that name are on sale.
</li><li><span style="font-weight:bold">list mine</span> - To see what items you have auctioned (both open and closed).
</li><li><span style="font-weight:bold">announce &lt;on|off&gt;</span> - Turn on or off new auction announcements.
</li><li><span style="font-weight:bold">bid &lt;lot number&gt; &lt;amount&gt;</span> - To bid on item.
</li><li><span style="font-weight:bold">requests</span> - To see if any players posted any equipment requests.
</li><li><span style="font-weight:bold">requests &lt;on|off&gt;</span> - Turn on or off request announcements.
</li><li><span style="font-weight:bold">help</span> - To receive quick help about available commands.
</li><li><span style="font-weight:bold">faq</span> - To see the list of most frequently asked questions (which is
also available through '<a href="auction_faq.php">help auction faq</a>').
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
