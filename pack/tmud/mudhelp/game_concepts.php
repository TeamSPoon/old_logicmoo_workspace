<h2>Game concepts (General Help)</h2><p>This help file is a short summary of all help topics in the 'general
help' category.  These are the things you should be generally aware
of when playing The Two Towers:
</p><ul><li><span style="font-weight:bold"><a href="helpers.php">helpers</a></span> - Helpers, or Newbiehelpers, are players who have volunteered 
to spend extra time helping new players get familiar with the game.
</li><li><span style="font-weight:bold"><a href="hints.php">hints</a></span> - How the in-game hints are structured and how they work.
</li><li><span style="font-weight:bold"><a href="experience.php">experience</a></span> - Your point reward for fighting.
</li><li><span style="font-weight:bold"><a href="alignment.php">alignment</a></span> - Your perceived "goodness" (or "evilness").
</li><li><span style="font-weight:bold"><a href="levels.php">levels</a>, <a href="levelling.php">levelling</a>, <a href="level_quests.php">level quests</a>, <a href="highlevels.php">highlevels</a></span> - Requirements for and 
detailed description on how to advance your character.
</li><li><span style="font-weight:bold"><a href="skills.php">skills</a>, <a href="skills2.php">skills2</a>, <a href="skills3.php">skills3</a></span> - Description of available character skills.
</li><li><span style="font-weight:bold"><a href="stats.php">stats</a></span> - Your physical and mental attributes.
</li><li><span style="font-weight:bold"><a href="hitpoints.php">hitpoints</a>, <a href="endurance.php">endurance</a></span> - Also known as HP and EP, your current health.
</li><li><span style="font-weight:bold"><a href="age.php">age</a></span> - What do we understand by "character age".
</li><li><span style="font-weight:bold"><a href="services.php">services</a></span> - What useful places you can find in most towns.
</li><li><span style="font-weight:bold"><a href="speedwalking.php">speedwalking</a></span> - How to travel faster.
</li><li><span style="font-weight:bold"><a href="combat.php">combat</a></span> - Quick description of the combat system.
</li><li><span style="font-weight:bold"><a href="pvp_system.php">pvp system</a></span> - How fighting other player-characters works.
</li><li><span style="font-weight:bold"><a href="death.php">death</a></span> - What happens when you die.
</li><li><span style="font-weight:bold"><a href="encumbrance.php">encumbrance</a></span> - How much you can carry before you become too encumbered.
</li><li><span style="font-weight:bold"><a href="drunkenness.php">drunkenness</a></span> - How does being drunk affect the gameplay.
</li><li><span style="font-weight:bold"><a href="spellcasting.php">spellcasting</a></span> - How spellcasting works in Arda.
</li><li><span style="font-weight:bold"><a href="tongues.php">tongues</a></span> - Description of different spoken languages in Arda.
</li><li><span style="font-weight:bold"><a href="sight.php">sight</a></span> - What happens when you cannot see.
</li><li><span style="font-weight:bold"><a href="channels.php">channels</a></span> - Channels, or comms, are the communication lines with other
players.
</li><li><span style="font-weight:bold"><a href="abbreviations.php">abbreviations</a></span> - Common abbreviations used by the MUD players in tells
and on comms.
</li><li><span style="font-weight:bold"><a href="parties.php">parties</a></span> - How to adventure together with other players.
</li><li><span style="font-weight:bold"><a href="semotes.php">semotes</a></span> - Detailed description of the soul emote system.
</li><li><span style="font-weight:bold"><a href="idle.php">idle</a>, <a href="lag.php">lag</a>, <a href="linkdead.php">linkdead</a></span> - How your connection can affect the gameplay.
</li><li><span style="font-weight:bold"><a href="reboots.php">reboots</a>, <a href="portals.php">portals</a></span> - Information about the cyclical "reboot" of the game 
world.
</li><li><span style="font-weight:bold"><a href="surnames.php">surnames</a>, <a href="titles.php">titles</a></span> - Some options to customize your character.
</li><li><span style="font-weight:bold"><a href="autoloads.php">autoloads</a></span> - Information about items that stay with you when you quit.
</li><li><span style="font-weight:bold"><a href="unique.php">unique</a></span> - Information about special, powerful items in Arda.
</li><li><span style="font-weight:bold"><a href="namechange.php">namechange</a>, <a href="racechange.php">racechange</a></span> - Details of special one-time changes you
can make to your character.
</li><li><span style="font-weight:bold"><a href="mud_alignment.php">mud alignment</a></span> - A system of global messages roughly representing
what NPCs get killed more in the MUD: the good ones or the evil ones.
