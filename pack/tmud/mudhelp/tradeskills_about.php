<h2>Tradeskills about (Tradeskills Help)</h2><p>The tradeskill system is designed to allow players to more fully
interact with their environment and players, and to make their own neat
stuff.  There are three different types of tradeskills: gathering,
crafting, and artisan.  For this example, we will walk you through the
process of making a loaf of bread.
</p><p>Making a loaf of bread from scratch requires the gathering and
crafting tradeskill types: You'll first need to harvest the wheat, mill
the wheat into flour, and then bake the flour (and other ingredients)
into bread.  You might use an artisan tradeskill such as pottery to
make a dish with which to serve the bread.
</p><p>Harvesting is essentially reaping items from the land, whether it
is wheat, iron ore, or lumber.  Often, you will need to refine those
materials into something more usable, and a crafting tradeskill will be
needed to accomplish that.  Artisan tradeskills are primarily used to
create something aesthetic and enriching, such as a piece of jewelry, a
portrait, or a plate.
</p><p>The following tradeskills are available in game right now:
</p><ol style="list-style-type:decimal"><li>Gathering:
<ul><li><span style="font-weight:bold"><a href="harvesting.php">harvesting</a></span> - Lets you harvest raw materials, such as wheat.
</li><li><span style="font-weight:bold"><a href="fishing.php">fishing</a></span> - Lets you use fishing equipment to catch fish.
</li></ul></li><li>Crafting:
<ul><li><span style="font-weight:bold"><a href="cooking.php">cooking</a></span> - Lets you prepare delicious food!
</li><li><span style="font-weight:bold"><a href="sewing.php">sewing</a></span> - Lets you craft articles of clothing.
</li><li><span style="font-weight:bold"><a href="alchemy.php">alchemy</a></span> - Lets you brew powerful potions.
</li></ul></li></ol><p>For each of them, you can see 'help (tradeskill name)' to learn
more about it.
</p><p>You will find various areas in which a tradeskill helper is available
to teach you new tradeskills, or to let you focus or forget about them.
Focusing a tradeskill makes it 'active' (seen as a * in the tradeskills
command).  If a tradeskill is not active, you cannot use it; further,
non-active tradeskills will degrade for each hour of age).  Professionals
(players who have a non-civilian profession) may not have as many active
tradeskills as civilians.  For the exact numbers, see '<a href="tradeskills.php">help tradeskills</a>'.
As such, you must choose wisely what tradeskills you wish to master.
</p><p>Mastering a tradeskill can only be accomplished through use.
Practice, practice, practice!  Even if you do not succeed in crafting,
you will probably learn something from it.
</p><p>Like the rest of The Two Towers, tradeskills are not a static thing.
As time goes on, new tradeskills will be added to the game and many
tradeskills may be used together to create even better things.
