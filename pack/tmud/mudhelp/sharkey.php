<h2>Sharkey (Roleplaying/Games Help)</h2><p>The game of sharkey is a party game, requiring several players to
participate.  It is run by Bob the hobbit living in the town of Staddle.
</p><p>The rules of this intriguing game are as follows:
</p><p>A group of players must sit in chairs around the table in order to
play Sharkey.  Once enough players gather, the wonderful game can begin.
They will be divided up into a group of evil Sharkeys and angry Hobbits.
Two of the hobbits are a doctor and a shirriff, although these
individuals' identities are unknown to the other people.  Although the
Sharkeys know who the other Sharkeys are, most Hobbits never learn who
is a Hobbit and who is not.  Some Hobbits are members of the Mathom
Society, and those Hobbits know they can trust one another.  You can
always stand up again to quit the game.
</p><p>Play commences in rounds, one by one.  Each round, the Sharkeys decide
who to murder.  The Doctor decides who to rescue from the Sharkeys.  The
Shirriff can identify one player's true identity.  One player is then
slain by the Sharkeys, unless that player was rescued by the Doctor, or
unless the Sharkeys couldn't agree on a player in time.  The Mathom Society
members may choose to induct one new member per round, by unanimous vote.
</p><p>Play then shifts to the collective players.  The players can choose
to lynch any player.  If they do not choose to lynch a particular player
in time, another round starts over again - although it may now be short
one player.  A Mathom Society member's choice to lynch counts for one and
a half times that of a normal player.
</p><p>Any player may leave a secret signed message for the Mathom Society
or for the Sharkeys.  Mathom Society members and Sharkeys can leave
messages for their own groups, too.
</p><p>The remaining Sharkeys win if they become a majority of the players.
The Hobbits win if all of the Sharkeys have been lynched.
</p><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
