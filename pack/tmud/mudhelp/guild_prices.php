first 25, each cosmetic room has a purchase price of 10,000 gold.
Rent is as follows:
<ul><li><span style="font-weight:bold">1st set of 5</span> - 2,500
</li><li><span style="font-weight:bold">2nd set of 5</span> - 3,500 
</li><li><span style="font-weight:bold">3rd set of 5</span> - 5,000
</li><li><span style="font-weight:bold">4th set of 5</span> - 7,500
