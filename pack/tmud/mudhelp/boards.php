<h2>Boards (Objects Help)</h2><p>The bulletin boards scattered around Arda are yet another form of
communication that is available for you.  Most boards are represented as
a stationary object in the room; you can distinguish them by the text
following their short description, like "[25 notes, 25 new]".
</p><p>All boards share the same commands:
</p><ul><li><span style="font-weight:bold">post [&lt;title&gt;]</span> - Post a new note.  Default title is '&lt;no title&gt;'.
To end a note, leave a period (.) on a line by
itself.
</li><li><span style="font-weight:bold">post [in &lt;language&gt;] &lt;title&gt;</span> - Post a new note in a language you know;
note that the syntax is very specific.  A title
is required in this case.
</li><li><span style="font-weight:bold">read board</span> - List all of the notes on the board; in many cases,
just 'look board' will have the same effect.
</li><li><span style="font-weight:bold">read &lt;num&gt;</span> - Read a specified note.
</li><li><span style="font-weight:bold">reply &lt;num&gt; [&lt;tl&gt;]</span> - Reply to specified note.  Default title is 
'Re: old title'
</li><li><span style="font-weight:bold">remove &lt;num&gt;</span> - Remove a specified note (only if you posted it).
</li><li><span style="font-weight:bold">edit &lt;num&gt;</span> - Edit text of a specified note (only if you posted
it).
</li><li><span style="font-weight:bold">bsearch &lt;text&gt;</span> - Search for notes containing given text.
</li><li><span style="font-weight:bold">archives</span> - List all archived notes for this board.
</li><li><span style="font-weight:bold">archives &lt;num&gt;</span> - View an archived note.
</li><li><span style="font-weight:bold">bsearch [-archives] &lt;text&gt;</span> - Search archives for given text.
</li></ul><p>At any time when '&lt;num&gt;' is a potential argument, specify the number
of a current note, use 'current' to refer to the last note you manipulated,
or 'next' to refer to the next note on the board.
</p><p>As boards are a public form of communication, board notes are subject
to '<a href="comm_policy.php">help comm policy</a>'.  This applies to guild boards as well.
