<h2>Sparring (PvP Help)</h2><p>Sparring is the act of two or more players coming together in combat
outside of the lawsys.  This means that this combat cannot be reported
in a courthouse.  It also means that players cannot die from sparring.
</p><p><span></span><span style="color:#f55">IMPORTANT NOTE:
Lawsys-free combat will only begin after the spar has "started".  If
you attack your sparring partner before the spar has begun, you will
still be held accountable for lawsys fines!</span><span>
</span></p><p>There are some necessary preconditions in order to spar:
</p><ul><li>You must not be in combat at all (having left the room of your attacker
still counts!) - if you were in combat, both you and your attacker must
both type "stop", have the crime reported in a courthouse, leave Mordor,
etc.
</li><li>You must not be in a lawless land or in a lawless state (being wanted).
</li><li>You need to have at least 80% of your maximum HP.
</li></ul><p>Sparring works slightly like parties: each spar "session" has its
leader and at least one member.  Session is started by the leader, who
then can invite players to it (see "<a href="spar.php">help spar</a>" for all the syntax details).
Once invitation phase is complete, the actual spar may be started with the
"spar start" command.
</p><p>While you are sparring, a number of things can make you leave the spar
session:
</p><ul><li>If you enter combat with anyone (player or NPC) not participating
in your spar session, your spar will be ended.  Mounts owned by
other spar session members do not count - they are treated as a
part of the spar.
</li><li>If you enter a lawless area, your spar will be ended.
</li><li>If your spar session has started but you do not engage in combat with
your sparring partners for 60 seconds, your spar will be ended.
</li><li>If you are too busy to fight for too long, your spar will be ended.
</li><li>If your health reaches zero, your spar will be ended; you will be
immune from other attacks for 30 seconds, so be quick in getting to
safety to recover.  If you are the last person standing in an active
spar, you will also receive this protection.
</li><li>If you type "spar stop" then your spar will be ended.
</li></ul><p>You needn't worry about being interrupted during a sparring match:
while you are actively engaged in combat with your sparring partners, you
cannot be attacked by any player.
</p><p>After a spar in which combat occurred (it was explicitly "started")
ends, participants will be prevented from sparring again for 5 minutes.
