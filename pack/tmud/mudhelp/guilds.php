<h2>Guilds (Guild Help)</h2><p>The Guilds are formal player organizations meant for roleplaying.
Each Guild has a different and unique theme and purpose in Arda which must
be approved by Valinor.  In order to learn more about a particular guild,
you should talk to one of its members, or visit the clan room in Bree or
Khemnahr.
</p><p>Unlike clans, each Guild is granted a guild hall which they may then
freely customize.  Apart from being a 'safe spot' that many find their
home on the MUD, the guild halls provide a number of very useful services
such as armouries or shops.  It is certainly worth it to become a member
of a guild!
</p><p>There are also several help files that have been made to help you
better understand the Guilds:
