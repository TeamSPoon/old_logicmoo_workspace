<h2>Pvp flag (PvP Help)</h2><p>The administration has long felt that a problem exists in the game:
that some players may be bullied by others via the combat system, with the
result being either death, merciful, or blessing.  Regardless of the
result, this bullying has had the result of directing some players away
from the game.  We have defined the problem in the following manner:
</p><ul><li>Players are forced into the Player vs Player Combat system without
their consent.
</li></ul><p>While it can be argued that this is the way the game is played, this
administration believes that the game may be played in other ways.
</p><p>We've tried to resolve this particular problem in the history through
various ways: we've deleted repeat offenders and those who sought to drive
other players away through their combative actions.  In more recent
history, we've instituted the Blessing of Mandos in hopes of preventing
these problems from occurring.  Sadly, none of these implementations have
had the desired effect of providing a safe and consensual environment away
from the problems of real life.  This is, and always shall be a fantasy
world free from the trappings of the mundane; to that effect, we believe
that our players should not be required to participate in fatal
interpersonal strife against their will.
</p><p>In order to resolve this problem, we have decided to change the manner
in which Player vs Player Combat is initiated:
</p><p>An opt-in two-state PvP flag has been created.  The states are 'on',
and 'merciful'.  A third 'off' setting was previously available but has
since been removed, leaving the prior two settings.  The PvP flag affects
gameplay in the following ways:
</p><p>Combat:
</p><ul><li>A player with their flag set to 'merciful' cannot die from combat
with another player.  Instead, they will be knocked unconscious as
if the killer were 'merciful' towards them.  While in this case
the killer cannot be fined for murder, they are still responsible
for the attempt.
</li><li>A player with their flag set to 'merciful' cannot kill another 
player.  Instead, the victim will always be only knocked unconscious
as in the above case.
</li></ul><p>Theft:
</p><ul><li>A player cannot steal from another player unless the thief has the
flag set to 'on'.
</li></ul><p>Contracts:
</p><ul><li>A player cannot be contracted unless the victim has the flag set 
to 'on'.
</li><li>A player cannot place a contract unless the contractor has the flag
set to 'on'.
</li></ul><p>These are the conditions which must be met in order to change the flag
status to 'less dangerous' (ie. on-&gt;merciful):
</p><ul><li>At least 24 hours of AGE must have passed since the flag was last
changed.
</li><li>Player must not be in combat (i.e. all parties must 'stop').
</li><li>Player must not be in a lawless land.
</li><li>Player must not be wanted, or in some other lawless state (such as 
a punishment).
</li><li>Player must be in a courthouse.
</li><li>At least 7 days (of realtime) have passed since the player's last
lawsys crime.
</li><li>Player must not have any pending lawsys fines or subpoenas.
</li><li>Player must not be contracted.
</li></ul><p>These are the conditions which must be met in order to change the flag
status to 'more dangerous' (ie. merciful-&gt;on):
</p><ul><li>At least 24 hours of AGE must have passed since the flag was last
changed.
</li><li>Player must not be in combat (i.e. all parties must 'stop').
</li><li>Player must not be in a lawless land.
</li><li>Player must not be wanted, or in some other lawless state (such as 
a punishment).
</li><li>Player must be in a courthouse.
</li></ul><p>Exceptions to the flag (these will make the flag count as 'on', but
will not turn it on per-se):
</p><ul><li>A player in a lawless land will be killable by anyone.
</li><li>A victim of a mutual guild war will be killable by the enemy 
guild members.
</li></ul>
