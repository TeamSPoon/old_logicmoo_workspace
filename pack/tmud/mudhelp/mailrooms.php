<h2>Mailrooms (Services Help)</h2><p>Most major cities and towns of Arda have a mailroom - a place where
mail comes in and from where it goes out into the world.  From gameplay
point of view, mailrooms are the only places where you can use the
in-game mail system (often referred to as 'MUD mail').
</p><p>To send and receive mail, simply go to any mailroom and type 'mail';
this will move you into the mailer subsystem.  If you are unsure how to
use it, just type '?' at the prompt for the list of available commands.
Be aware that Evil Race mailrooms will refuse service to Free Race
players and vice versa.  Furthermore, if you are known as a murderer in
the mailroom's town, you may be refused service as well.
</p><p>If you want a more 'roleplaying' way of using mail, you may use
actual in-game note objects available in many places around Arda.  To
do so, simply 'write' a text on your note, then go to a mailroom and
'deliver note'.
</p><p>You may send mail to any player or Ainu by specifying their name as
the recipient.  For players, you may also specify their full MUD mail
address (see below for details), although it is not necessary.  In
addition to that, you may also send mail to the following:
</p><ul><li><span style="font-weight:bold">'friends'</span> - will send mail to all people on your friends list.
</li><li><span style="font-weight:bold">'guild'</span> - will send mail to all members of your guild
</li><li><span style="font-weight:bold">'clan'</span> - will send mail to all members of your clan
</li><li><span style="font-weight:bold">'gm#'</span> - will send mail to the guildmaster of the guild with number 
'#'.  The guild numbers may be viewed in Bree and Khemnahr
clan rooms.
</li></ul><p>You cannot send mail to someone that is ignoring you, nor can you send
mail to someone that you are ignoring.
</p><p>Apart from their function to send and receive mail, mailrooms are
used as a roleplaying tool to let you associate yourself with the given
town/community in Arda.  To do so, you should 'register' at the mailroom
in that town.  Doing so costs 10 gold, and will result in receiving a
MUD mail address which will be visible in your legend.  For example, if
your name is Elmo, and you have registered at the post office in Bywater,
your MUD mail address would be Elmo@Bywater.  While it is not necessary
to register anywhere to use the mail system, we recommend to you to do so
to enhance roleplay of yourself and others.
</p><p>If you no longer wish to use your address, you may either 'unregister'
at the same mailroom, or simply register at another to change.
