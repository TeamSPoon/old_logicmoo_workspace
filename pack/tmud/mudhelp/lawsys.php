<h2>Lawsys (PvP Help)</h2><p>T2T maintains a complex lawsys to manage PvP actions.  This is the
breakdown of that system, providing both an outline of the crimes
themselves and the prosecution process.
</p><h3>Lawsys procedure</h3><ol style="list-style-type:decimal"><li>You commit a crime.
<ul><li>If the crime is committed inside of Mordor or Minas Morgul,
which are considered lawless, the crime is not logged and is
not reportable.
</li><li>Otherwise, the crime is logged.
</li></ul></li><li>Victim discovers who committed the crime.
<ul><li>If the victim is unsure who committed the crime, they can ask
someone to investigate it for them.
<ul><li>Players skilled in the "intuition" skill can investigate a
crime and try to determine who did it.  Crime victims cannot
investigate themselves.
</li><li>This is possible with both thefts and murders.
</li></ul></li></ul></li><li>The crime is reported to a courthouse by the victim.
<ul><li>There is a statute of limitations. If a crime isn't reported
within 7 RL days, then it is no longer reportable.
</li><li>Only the victim can report the crime.
</li><li>The victim must be able to name a suspect.
</li><li>If an attempted murder was committed, the victim must report
BEFORE logging off or going linkdead.  If they fail to do so,
the crime is no longer "valid" and will count as a false report
if reported anyway.
</li><li>Reporting the crime or making it unreportable (victim quitting
or going link-dead) stops combat between the victim and the
attacker.  This of course applies only when the crime was
reportable to start with, that is Mordor combat and attacking
wanted players are excluded from this rule.
</li></ul></li><li>You are ordered to go to the court (if you are a FR) or to a commander
(if you are an ER) to respond to the accusation.
<ul><li>You will be told the charge and that you must enter a plea.
</li><li>You have 1 hour of play time to report to the court/commander.
A login message will remind you.
</li><li>However, if the victim changes their mind, they can "withdraw
report" and cancel the subpoena.
</li><li>If you fail to report to the judge/commander within the hour,
the system will "plead" for you automatically; you will be found
guilty or innocent depending on whether you actually committed
the crime.
</li></ul></li><li>You report to the nearest judge/commander and enter your plea.
<ul><li>If you are not the person who committed the crime and you plead
innocent, then you get off and you can report the "victim" for
a false report.
</li><li>If you did commit the crime and still plead not guilty, you have
a small chance of not being convicted (5%), but if you fail that
chance, the fines will be higher (10%).
</li><li>You can also attempt to bribe the judge; if successful, this will
treat you as guilty, but your fine will be lower.
</li><li>If you plead guilty (or you are found guilty), you learn your fine
amount and the time period in which to pay it.
</li></ul></li><li>Once you are found guilty, there is a certain time period in which you
have to pay your fine.
<ul><li>You will be reminded about the remaining time periodically, as well
as during logins should you quit before paying your fine fully.
</li><li>When you pay the fine, the lawsys case is over, and the victim
will receive their compensation through the "claim reimbursements"
command in a courthouse.
</li><li>If you fail to pay the fine within the set time, you will become 
wanted.
</li></ul></li><li>Once you become wanted, you are outside the law.
<ul><li>Wanted posters with your name will go up in all the post offices.
</li><li>Any FR player may "deputize" in a courthouse and try to "handcuff"
you.  ER players can "volunteer" at the commander and then attempt
to "shackle".
</li><li>Any PvP-on player can now also kill you with no lawsys penalties!
</li><li>Hiding in places where no deputy can arrest you is ILLEGAL, and
will be punished accordingly to "<a href="bug_abuse.php">help bug abuse</a>".  Note that some
locations will not allow your wanted time to decrease.  In these
locations, your presence is not considered bug abuse.
</li><li>You will be wanted for exactly 1d of your character age.  If you
manage to avoid being caught within that period, you will be
"free" again.
</li></ul></li><li>You are handcuffed by a deputy.
<ul><li>Once handcuffed, you, the criminal, are locked into combat with
the deputy.  Neither of you will be able to move, until:
<ol style="list-style-type:lower-alpha"><li>If your hit points fall too low the deputy hauls you off to a
jail cell.
</li><li>If the deputy's hit points fall too low, the handcuffs are
broken and you (or the deputy) may escape.  To make it easier,
after a failed handcuff attempt both you and the deputy
will be protected from other player's attacks for 2 minutes.
</li></ol></li></ul></li><li>If you were caught, you will be taken to a cell.
<ul><li>After a short trial, the fine will be collected from you "by 
force".  If you don't have enough gold on your person and in your
bank account, your stats will be lowered through torture until
their retrain cost will match the fine.  It is very likely that
you will emerge as a ghost from this process.
</li><li>It is illegal to abuse the lawsys to "drain" one's character, or 
otherwise convert one's arrests into monetary gain.
</li></ul></li></ol><h3>Crime breakdown</h3><ul><li><span style="font-weight:bold">False Report</span> - Wrongly accusing someone of a crime.  Make sure you
know who committed it before you report.  You will have 3 hours to
pay the fine for a false report.
</li><li><span style="font-weight:bold">Attempted Theft</span> - A failed attempt to take belongings.  Thieves get
a 10% discount off the bribe cost for this offense.  You will have 2
hours to pay the fine for an attempted theft.
</li><li><span style="font-weight:bold">Theft</span> - Taking things from an unwilling person.  No bribing can be
done for this crime.  You will have 2 hours to pay the fine for a
theft.
</li><li><span style="font-weight:bold">Attempted Murder</span> - A failed attempt on a player's life.  Assassins
get a 10% discount off the cost of a bribe for this offense.  You
will have 10 minutes to pay the fine for an attempted murder.
Attempted murder fines are capped at 5000 gold.
</li><li><span style="font-weight:bold">Murder</span> - Taking the life of another player other than in self-defense.
You may not bribe the judge for this offense.  You will have 10
minutes to pay the fine for a murder.  Murder fines are capped at 10000
gold.
</li></ul><p>Attacking a mount when someone is riding it is not considered attempted
murder.  Killing a horse or other mount, however, is considered an act of
theft against the owner of the mount and is reportable as such (theft).
The owner of the horse must be online for the act to be a crime.
</p><h3>Emotes of crimes</h3><p>To keep from being fooled by crime emotes, look for the following
messages, none of which can be emoted:
</p><pre>You catch XXX trying to steal YYY from you!
You noticed that XXX stole YYY from you!
You are attacked by XXX!
Without warning, XXX stabs you from behind!
