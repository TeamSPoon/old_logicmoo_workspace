<h2>Autoloads (General Help)</h2><p>Autoloading items, or autoloads for short, are special items that
stay with you all the time, even when you quit.  This is unlike any
other item that will be dropped on the ground when you leave the game.
</p><p>For most part, autoloads are purely cosmetic items - they only serve
to enhance the look of your character or help you in roleplaying.  However,
many players gather them just to "show off" their collection to others.
Some autoloads are quite rare and are very prized possessions of their
owners; we have quite a few collectors amongst the playerbase who go to
great lengths to obtain the one item they want.
</p><p>The most common autoloading items you will come across are pipes and
musical instruments; they can be bought at shops in newbie towns.  The
survival gear that is handed out during the newbie tutorial is also
autoloading, but only until you reach level 5.  To learn about the others,
you will have to explore Arda and find them on your own, or gather the
information from other players.
