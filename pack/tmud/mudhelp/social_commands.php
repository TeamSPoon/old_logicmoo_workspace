<h2>Social commands (Command Help)</h2><p>Since The Two Towers is a multiplayer game, communication with other
players is one of its core functions.  We offer plenty of ways to do
so, with numerous customization options.  Here is a brief list of commands
that you can use to communicate and otherwise interact with others.
</p><ul><li><span style="font-weight:bold"><a href="say.php">say</a>, <a href="mutter.php">mutter</a></span> - The most basic communication commands.  Makes your 
character say (or just mutter) something!
</li><li><span style="font-weight:bold"><a href="tell.php">tell</a>, <a href="respond.php">respond</a>, <a href="tellhist.php">tellhist</a>, <a href="privacy.php">privacy</a></span> - Commands used for communication 
with players that are not in the same room as you are.
</li><li><span style="font-weight:bold"><a href="emote.php">emote</a>, <a href="emoteat.php">emoteat</a></span> - Sends a message to everyone in your room that
your character is doing something, like smiling or nodding.
</li><li><span style="font-weight:bold"><a href="semote.php">semote</a></span> - While you can use fully custom emotes as described above 
to show your character doing something, we have also prepared
a very large list of pre-defined emotes, each acting as a command
on its own.  Use this command to view the full list.
</li><li><span style="font-weight:bold"><a href="whisper.php">whisper</a></span> - A cross between "say" and "tell" - lets you send a private
message to just one person in the room you are in.
</li><li><span style="font-weight:bold"><a href="shout.php">shout</a></span> - Sends a message to everyone in your room as well is other
rooms in the area you are currently in.  Use sparsely, as excessive
shouting may be annoying to others.
</li><li><span style="font-weight:bold"><a href="who.php">who</a></span> - Lists all players who are online.
</li><li><span style="font-weight:bold"><a href="legend.php">legend</a></span> - Lets you view detailed information about a particular 
player, online or not.  This command is often called "finger" on
other MUDs.
</li><li><span style="font-weight:bold"><a href="story.php">story</a></span> - Allows you to view the character story that another player has
written.
</li><li><span style="font-weight:bold"><a href="comms.php">comms</a>, <a href="comm.php">comm</a>, <a href="newbiecomm.php">newbiecomm</a>, <a href="er.php">er</a>, <a href="fr.php">fr</a>, <a href="clan.php">clan</a>, <a href="guild.php">guild</a>, <a href="trivia.php">trivia</a></span> - These commands
let you list and use the public chat lines we have available, or
"comms" as they are called here.
</li><li><span style="font-weight:bold"><a href="party.php">party</a></span> - A command interface for adventuring together with other 
players.  This command has a lot of possible arguments that let
you create and manage parties; see the command help itself for all
the details.
</li><li><span style="font-weight:bold"><a href="friend.php">friend</a>, <a href="enemy.php">enemy</a></span> - During your stay on The Two Towers, you will 
undoubtedly make new friends, as well as probably get to know people
you wish you didn't!  These commands let you manage lists of your
friends and enemies so that you don't forget who they are.
</li><li><span style="font-weight:bold"><a href="ignore.php">ignore</a>, <a href="unignore.php">unignore</a></span> - While we strive to create a helpful and friendly
atmosphere on The Two Towers, we understand that some people just
do not get along together well and may no longer wish to speak to each
other.  These commands let you block (and unblock) all communication
with certain players.
</li><li><span style="font-weight:bold"><a href="converse.php">converse</a></span> - This command puts you in a special "converse" mode, where
everything you type is interpreted as a "say".
</li><li><span style="font-weight:bold"><a href="overthrow.php">overthrow</a>, <a href="plea.php">plea</a></span> - These two commands allow special interactions within
player guilds; see their own help files for more details.
</li><li><span style="font-weight:bold"><a href="kudo.php">kudo</a>, <a href="rpscore.php">rpscore</a></span> - We have a system where you can reward another player
for good roleplaying - "kudo" them when you like what they do, and
check "rpscore" to see your own kudo points.
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
