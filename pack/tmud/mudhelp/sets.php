<h2>Sets (Technical Help)</h2><p>This help file details all the environment variables you can set on The
Two Towers.  They control various things, from the text display to how your
character can behave in certain situations and react to certain events.
All these variables may be set with the "set" command.  Also note that most
of them can also be manipulated through the more user-friendly interface of
the "settings" command.
</p><ol style="list-style-type:decimal"><li>General MUD display configuration:
<ul><li><span style="font-weight:bold">prompt</span> - Set your prompt.  See "<a href="prompt.php">help prompt</a>" for more details.
</li><li><span style="font-weight:bold">newline</span> - Adds a newline before all messages.  Fixes linewrap on some
older clients.
</li><li><span style="font-weight:bold">LINES</span> - Sets the number of lines printed before --MORE--(%).  If you
use a negative value and your client supports the TELNET protocol,
more will use all the lines of your screen except LINES.  It cannot
be set higher than 200.
</li><li><span style="font-weight:bold">clear_more</span> - better looking --MORE--, but doesn't work on some 
clients.
</li><li><span style="font-weight:bold">menu_page_length</span> - This allows you to modify menus to display however
many items on the list per page as you select.  The default is 8, but
you could set this to anything from 3 to 99.
</li></ul></li><li>Specific configuration of output of some commands:
<ul><li><span style="font-weight:bold">armour_verbose</span> - When set to anything, the "armour" command will 
display in verbose mode by default.
</li><li><span style="font-weight:bold">armour_other</span> - When set to "off", the "armour" command will not 
display your autoloading items (protecting "other" area).
</li><li><span style="font-weight:bold">tellhist_timestamps</span> - Set this to "on" to show timestamps in your 
tellhist.
</li><li><span style="font-weight:bold">whodefault</span> - Set this to your default "who" preferences.
</li><li><span style="font-weight:bold">whocolor</span> - If set to "on" it will turn on the display of colored
titles in the who list.
</li><li><span style="font-weight:bold">gm_sort</span> - Set how you would like "legend gm" to be listed.  Setting
this variable to "gm" will sort alphabetically by GM name.  Any
other setting (including no setting at all) will sort alphabetically
by guild name.
</li><li><span style="font-weight:bold">comm_timestamps</span> - When set to "off", you will not see timestamps in
any comm history.  When set to "diff", the comm timestamps will
show you the time that has passed since the comm was made rather
than giving absolute time.  The default (anything but "off" or
"diff") enables standard timestamps.
</li><li><span style="font-weight:bold">commhist_color</span> - When set to "off", history of comms won't be 
coloured.
</li><li><span style="font-weight:bold">full_exit_color</span> - When set to "on", the commas and "and" word in 
room exits' description will be coloured with the exits/water_exits
color; normally they are left in the standard color.
</li></ul></li><li>Settings specific to particular items/subsystems:
<ul><li><span style="font-weight:bold">show_all_crimes</span> - Normally, the courthouse's "check reports" command
will not show crimes committed by people who did not log on for 30
or more days.  Setting this variable bypasses that check and shows
all crimes.
</li><li><span style="font-weight:bold">no_medalclip</span> - When set, medals from RP battles won't be clustered.
</li><li><span style="font-weight:bold">quest_messages</span> - When not set, you will not receive any quest 
completion messages.  When set to "new", you will receive a message
when you complete a quest for the first time.  When set to "on",
you will receive a message every time you complete a quest.
</li><li><span style="font-weight:bold">announce_friends</span> - If set, you will receive announcements when people
on your friends list who also have you on their friends list log into
the game.
</li><li><span style="font-weight:bold">mail_wrap</span> - When set, all incoming mail will be wrapped.
</li></ul></li><li>Settings related to suppressing some messages you receive, to make the
MUD output easier to read:
<ul><li><span style="font-weight:bold">briefcombat</span> - If set, you will not hear combat messages other than 
your own (swings targeted at you and from you).
</li><li><span style="font-weight:bold">briefcommands</span> - If set, you will stop receiving command messages 
about multiple matching items.
</li><li><span style="font-weight:bold">bowmessages</span> - If this is set to "off", you won't see your own draw, 
nock, and shoot messages in combat.
</li><li><span style="font-weight:bold">ignore_eaglesight</span> - If set, you will not receive warning messages
from the eaglesight command.
</li><li><span style="font-weight:bold">hide_party_movement</span> - Set this to minimize party movement messages.
</li><li><span style="font-weight:bold">lead_messages</span> - If set to "off", you will not receive the 
"You lead ..." message when you lead a mount or pull a wagon.
</li><li><span style="font-weight:bold">no_room_tags</span> - If you are a Guildmaster and do not wish to see the
entrance settings for rooms of your guildhall (e.g. [public]), then
set this to hide them.
</li></ul></li><li>Setting related to communication with other players:
<ul><li><span style="font-weight:bold">afk</span> - If you are idle, the message from this setting is what people 
get when they "tell" you.
</li><li><span style="font-weight:bold">exempt_top</span> - Set this to exempt yourself from "legend top" listings.
</li></ul></li><li>Settings affecting behaviour of your character:
<ul><li><span style="font-weight:bold">autowield</span> - If this is set to "off", you won't wield when you take 
a weapon.
</li><li><span style="font-weight:bold">automount</span> - If set to "off", you won't remount your horse when you
return to it.  Professional riders may set it to "always" to auto-
remount even in combat (at the cost of two busy rounds).
</li><li><span style="font-weight:bold">autosheathe</span> - If set to "off", you won't auto-remove your wielded
weapon even if it's sheathable.
</li><li><span style="font-weight:bold">autoshoot</span> - If this is set to "off", you won't use draw, nock, and
shoot a bow automatically in combat.
</li><li><span style="font-weight:bold">autolead</span> - If set to "on", you will automatically start leading a
mount you dismount, unless you are already leading another one.
</li></ul></li><li>MXP settings:
<ul><li><span style="font-weight:bold">mxp</span> - Determines how the MUD will detect your client's MXP support:
<ul><li>When not set, MXP will be detected based by your client type
(client type can be viewed in "conninfo" command).  Currently,
client types that are recognized to know MXP are "cmud", "zmud",
"mushclient" and The Two Towers web client ("JSMC").
</li><li>When set to "on", MUD will always assume that your client supports
MXP and perform on-login MXP negotiation regardless of client
type.
</li><li>When set to "off", MUD will always assume that your client does
not support MXP and will not attempt to negotiate it.
</li></ul></li><li><span style="font-weight:bold">mxp_exit_links</span> - When set to "off", MXP hyperlinks will not be 
created for room exits.
</li><li><span style="font-weight:bold">mxp_object_links</span> - When set to "off", MXP hyperlinks will not be 
created for objects within room's contents.
</li><li><span style="font-weight:bold">mxp_inventory_links</span> - When set to "off", MXP hyperlinks will not be 
created for objects displayed in the "inventory" command.
</li><li><span style="font-weight:bold">mxp_door_links</span> - When set to "off", MXP hyperlinks will not be 
created for doors mentioned in room's detailed description.
</li><li><span style="font-weight:bold">mxp_board_links</span> - When set to "off", MXP hyperlinks will not be 
created for board/gossiper posts.
</li><li><span style="font-weight:bold">mxp_more_links</span> - When set to "off", MXP hyperlink will not be 
created for the --More-- prompt.
</li><li><span style="font-weight:bold">mxp_help_links</span> - When set to "off", MXP hyperlinks will not be 
created when viewing help files.
</li><li><span style="font-weight:bold">mxp_hp_ep_bars</span> - When set to "off", MXP-based HP and EP gauges 
will not be updated (although your client may still display them
if it "remembered" their existence from previous session).
</li><li><span style="font-weight:bold">mxp_messages</span> - When set to "off", MXP (HTML color, changing font
type/size on some clients) will not be present in "color" command
menu. If you have previously set some of your message classes to
use MXP, these settings will be ignored.
</li></ul><ul><li><span style="font-weight:bold">mxp_images</span> - When set to "off", inline images will not be displayed
(and you will see the default ASCII-art version instead).
</li></ul></li><li>Facebook settings:
<ul><li><span style="font-weight:bold">facebook</span> - Specifies whether you want the MUD to post messages about
your character's progress on your facebook wall.  Set it to "on"
to allow this, leave unset if not.  Note that this setting will
only have any effect if you are connecting to The Two Towers from
the web client (while logged on facebook) or from facebook
application.  When this setting is effect, you may use any of the
following settings to limit the types of messages posted:
</li><li><span style="font-weight:bold">fb_level_up</span> - When set to "off", the MUD will no longer post "you 
gained a level" messages.
</li><li><span style="font-weight:bold">fb_prof</span> - When set to "off", the MUD will no longer post "you 
have changed your profession" messages.
</li><li><span style="font-weight:bold">fb_quests</span> - When set to "off", the MUD will no longer post "you 
have completed another 25 quests" messages.
</li><li><span style="font-weight:bold">fb_guild</span> - When set to "off", the MUD will no longer post "you 
joined a guild" messages.
</li></ul></li></ol><p>Apart from the above, with the "set" command, you can also set the
default behaviour for semotes.  For example, typing "set grin evilly"
will cause you to always grin evilly.
</p><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
