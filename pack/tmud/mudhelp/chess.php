<h2>Chess (Roleplaying/Games Help)</h2><p>Chess is a two-player game between the forces of Good and Evil.  Good
is represented by the White pieces and Evil is represented by the Black
pieces.  While it is not very Tolkien-thematic, we still offer players to
play chess in Arda as an OOC form of entertainment.  Currently, there are
two public chess tables in Arda: one is in the bar of Thranduil's caverns
in Mirkwood and the other in Asturasartes, the school of necromancers in
Far Harad.
</p><p>There are six different types of pieces: pawns, knights, bishops,
rooks, queens, and kings.  The movements of the pieces are as follows:
</p><ul><li><span style="font-weight:bold">Pawn</span> - The Pawn is the weakest piece.  It usually can only move one 
square forward.  If another piece is already in that square,
it cannot move forward at all.  On its first move it can move
two squares.  To capture a piece of the opposite color it can
move forward diagonally onto the square in question.
</li><li><span style="font-weight:bold">Knight</span> - The Knight can move two squares horizontally or vertically and 
one square in a perpendicular direction.  A knight near the
board's center thus has 8 L-shaped moves.  A knight in the
corner only has 2.
</li><li><span style="font-weight:bold">Bishop</span> - The Bishop can move diagonally as far as it wishes.  It can 
capture enemy pieces by moving on top of them.  It cannot
"jump over" a piece.
</li><li><span style="font-weight:bold">Rook</span> - Like the Bishop, but moves horizontally and vertically instead.
</li><li><span style="font-weight:bold">Queen</span> - Combines the moves of the Bishop and Rook.  Usually the most 
powerful piece.
</li><li><span style="font-weight:bold">King</span> - The King can move one square in any direction.  It is not the 
most powerful piece but as its name indicates it is the most
important.  You lose the game if you lose your King.
</li></ul><p>A pawn reaching the opponent's end of the board can "promote" to a
knight, bishop, rook, or queen.
</p><p>If the King is attacked by another piece, it is said to be "in check".
If you are in check, you MUST make some move to get out of check.  If no
such move exists, you have been "checkmated" and lose.  If you are not
in check but all your moves place you in check it is called a "stalemate"
and the game ends in a draw.
</p><p>If the king has not yet moved, it can "castle" either kingside or
queenside.  The rook on that side of the board must also not have moved
yet.  Castling consists of moving the king two squares towards the rook
and then moving the rook over one square past the king.  You cannot
castle if your king is in check, would have to move through check, or
would end in check.
</p><p>A few more minor rules exist:
</p><ol style="list-style-type:decimal"><li><span style="font-weight:bold">En passant</span> - If a pawn moves two squares forward, the next turn, and 
the next turn only, another pawn of the opposite color can capture
it as if it had only moved one square forward.
</li><li><span style="font-weight:bold">50-move rule</span> - If 50 moves pass without a pawn move or capture, the 
game is a draw (50 moves by each side that is).
</li></ol><p>To learn the exact syntax of commands used to play chess in The Two
Towers, visit one of the chess boards and read the plaques there.
