<h2>Clan stages (Guild Help)</h2><p>There are several stages that a clan may go through on its way to
becoming a fully fledged guild.
</p><ol style="list-style-type:decimal"><li><span style="font-weight:bold"><span></span><span style="color:#55f">forming</span><span></span></span> - This stage is the initial stage, immediately after 
registering the clan, where the clanmaster recruits people to join the
clan to meet the minimum member limit of 5 members in order to become
a proposed clan.  It costs 1000 gold to register a clan.
</li><li><span style="font-weight:bold"><span></span><span style="color:#5ff">proposed</span><span></span></span> - Once the forming clan meets the minimum 5 members, the 
clanmaster upgrades their clan to 'proposed' stage.  At this stage,
the Guild Liaison looks over the basic idea behind the clan (its
description) and judges whether or not such a group could viably exist
in the setting of the game.  He/she will either approve, deny, or
request that the description be changed a little using the 'changedesc'
command in the clan room.
</li><li><span style="font-weight:bold"><span></span><span style="color:#ff5">approved</span><span></span></span> - If the proposed clan is approved, the clan is upgraded to
this state automatically, waiting for the clanmaster to upgrade it to
full clan status.  It costs 10,000 gold to upgrade an approved clan.
</li><li><span style="font-weight:bold"><span></span><span style="color:#f55">denied</span><span></span></span> - If the proposed clan is denied, the clan is downgraded to
this state automatically, waiting for the clanmaster to disband it.
If the clan's leader does not disband it within five days, the clan
will be deleted automatically.  Once in this stage, there is no way
for the clan to continue existing or to redeem itself - it must be
deleted one way or another.
</li><li><span style="font-weight:bold"><span></span><span style="color:#b00">probation</span><span></span></span> - <span>If a clan does not manage to keep its membership above
the minimum level of five members, they are downgraded to probation
stage automatically.  Periodic checks are made on clan membership
levels.  The only way out of probation is to bring the membership
level back to five or above.  After spending five days in this stage,
the clan is automatically deleted. </span><span style="color:#fff">If you enter probation, your clan</span><span>
</span><span style="color:#fff">time will restart when you leave probation.</span><span>
</span></li><li><span style="font-weight:bold"><span></span><span style="color:#5f5">clan</span><span></span></span> - <span>This is the main stage of a clan's life - the actual clan 
stage.  In this stage the clan can enjoy a comm line and use of the
'mailclan' feature in the clan room.  The clan must always keep their
membership level above 5 members in order to stay out of probation
stage.  A clan may edit its description at any point in the clan room;
however, the clan will be changed back to </span><span style="color:#5ff">proposed</span><span> status and
reapproval by the Guild Liaison will be required.  Upon reapproval,
the clan's existence time will be restarted.
</span></li><li><span style="font-weight:bold"><span></span><span style="color:#f55">awaiting guildap</span><span></span></span> - When the clanmaster uses the 'upgrade' command
in the clan room to request a guild application form, the clan is
upgraded to this stage.  This stage has no special features - it is
just the clan stage with a different name so the Guild Liaison can
see who is filling out forms.  A clan may only upgrade after existing
for six months and having at least ten members.
</li><li><span style="font-weight:bold"><span></span><span style="color:#ff5">guildap submitted</span><span></span></span> - When the clanmaster submits his/her guild 
application, the clan is upgraded to this stage.  The Liaison will
now look through the details of the application.  Other than checking
consistency with the clan's description, the Liaison does not judge
the content of the application, only the viability of the data within
our system.  He/she may offer feedback and suggest revisions.
</li><li><span style="font-weight:bold"><span></span><span style="color:#bb0">awaiting fixes</span><span></span></span> - If there is something amiss with the guild 
application, the Liaison will mark it as awaiting fixes, and will mail
the details of what needs to be tweaked.  Once fixed, the guild
application may be re-submitted, and the clan will return to the
previous stage.
</li><li><span style="font-weight:bold"><span></span><span style="color:#5f5">guildap proposed</span><span></span></span> - When the Liaison approves a clan's guild
application, it is upgraded to this stage.  At this time, the
application will be discussed by Valinor as a proposal.  Clanmasters
may read the discussion and add comments.  See '<a href="guild_new.php">help guild new</a>' for
further information.
</li><li><span style="font-weight:bold"><span></span><span style="color:#5f5">guildap approved</span><span></span></span> - If Valinor approves a clan's guild application,
it is upgraded to this stage.  The clanmaster must now use the
'purchase hall' command in the clan room to obtain a basic guildhall.
The cost of this is 500,000 gold.
</li><li><span style="font-weight:bold"><span></span><span style="color:#fff">guild</span><span></span></span> - Once a guildhall has been purchased, a clan automatically
becomes a full-fledged guild.  The Gridmaster and Guilds Liaison
will install the guildhall at their earliest convenience, and the
clanmaster will be made a guildmaster immediately.  A guild's
vault balance must remain above zero and its membership above ten
at all times.  If these requirements are not met for three months
in a row, it will be automatically demoted to clan status again.
</li></ol><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
