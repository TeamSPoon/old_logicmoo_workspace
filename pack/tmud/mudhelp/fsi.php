<h2>Fsi (Rules and Policies)</h2><h3>Frequently Shot-down Ideas</h3><p>This is a list of frequently proposed ideas and the reasons we have
chosen not to implement them.
</p><p style="font-style: italic">"We should be able to wield multiple weapons/hit specific body target
areas/(insert other combat enhancement here)."
</p><div style="margin-left: 20px"><p>Most (MOST, not ALL) combat enhancement ideas are turned down because
of speed issues.  We made the decision early on that we would try to
keep combat simple because it is one of the most frequently used
portions of code on the game.  At any one time, lots of people are in
combat.  This means that even a seemingly minor addition to the code
can cause significant slow downs (lag). That doesn't mean every idea
here is a bad one... there are some rare exceptions.
</p></div><p style="font-style: italic">"We should be able to create custom emotes for combat or spellcasting."
</p><div style="margin-left: 20px"><p>It should always be clear when someone is attacking you.  Customizing
these kinds of emotes would create ambiguity, increase CPU usage
(increase lag), and be difficult to police for profanity and
inappropriate content.
</p></div><p style="font-style: italic">"I should be able to decide at what level of hit points 'wimpy' kicks
in and/or the direction in which I flee."
</p><div style="margin-left: 20px"><p>In real combat, you take whatever opportunity you can to escape with
your life.  Being able to choose the exact moment and the direction
is not realistic, as it will depend on how your foe is positioned and
how the last blow affected you.
</p><p>The wimpy command is a convenience, not a guarantee.  Allowing wimpy
to be configured in this way makes a character's actions controlled
more by AI than typist interaction.  We do not wish to encourage this
kind of "botting" approach to playing this game.  Wimpy can save your
life when you are lagged, but you should not count on it in every
instance.
</p></div><p style="font-style: italic">"We should be able to rent an inn room for more than one day at a time."
</p><div style="margin-left: 20px"><p>This is an inn room, not an apartment.  Since our setting is the same
'day' over and over again, renting a room for more than one day is
not particularly realistic!
</p></div><p style="font-style: italic">"Being drunk should affect your speech, your ability to read/understand
others, or how quickly/accurately you can travel."
</p><div style="margin-left: 20px"><p>Drunkenness already adversely affects such things as combat, swimming,
and awareness.  Since drinking alcoholic beverages is a fairly popular
form of healing, making words less intelligible or travel more
difficult would seriously hamper playability.
</p></div><p style="font-style: italic">"We should have an arena/area where we can fight each other and not be
killed/not lose our stuff/not be reported/etc."
</p><div style="margin-left: 20px"><p>After having been a FSI for many years, in the fall of 2008, based on
player survey feedback, the Administration has decided to give the
arena idea a try.  The entry is left here for the sake of tradition.
</p></div><p style="font-style: italic">"I think &lt;insert town/area name&gt; should be coded/put back in game."
"I think there should be a quest to obtain &lt;item&gt;."
</p><div style="margin-left: 20px"><p>Idea reports like this will always be thrown onto the "Idea Pile" and
not rewarded.  Everyone here already has their hands full with their
own projects.  All towns and other areas are known to us and are
intended to be done, but they all have to wait in line.  These kind
of reports are just frustrating.  As for creating new quests, these
take a *really* long time to do, and Ainur prefer making quests that
they thought of rather than ones you imagined.  So, if you want a
quest coded, see "<a href="immorting.php">help immorting</a>"!
</p></div><p style="font-style: italic">"I think we should have a &lt;insert verb here&gt; emote."
</p><div style="margin-left: 20px"><p>Don't waste your time with these ones.  They are just deleted ;).
</p></div><p style="font-style: italic">"The money we deposit in banks should accumulate interest."
</p><div style="margin-left: 20px"><p>This idea has been discussed in the past.  If you think of banks in
the modern sense of the word, you would be right.  But in medieval
society, there were not what we today call banks.  There were two
separate institutions that served the most basic functions of
today's banks:
</p><ul><li>Moneylenders, who would loan you money and charge you a fee.
</li><li>Vaults and strongholds, which would safekeep your money and charge
you a fee.
</li></ul><p>In medieval times, you paid to borrow money, and you paid to have
your money kept safe from thieves.  There was no concept of
reverse-lending (depositing money so the bank can make money off of
it and returning a small profit to you for the favor).
</p><p>So our banks here *charge* you to safekeep your money.
</p></div><p style="font-style: italic">"We should have a 'focus' command for professional or non-combat skills."
</p><div style="margin-left: 20px"><p>Using a professional skill implies focusing on it.  The reason the
combat focus is an exception is because it is a defined set of skills
that you can prioritize during combat.  In short, focusing is already
automatic for all other skills.  Whenever a skill is checked it is
assumed you are using it to the absolute best of your ability.
</p></div><p style="font-style: italic">"We should have 'who female' and 'who male'"
</p><div style="margin-left: 20px"><p>This could easily lead to sexual harassment.
</p></div><p style="font-style: italic">"No one should be able to see legend or titles of other people without
being properly introduced or &lt;insert some other requirement here&gt;"
</p><div style="margin-left: 20px"><p>This is a hard tradeoff between realism and playability.  While this
would increase roleplaying, it would make the game more annoying to
people who do not want to roleplay.  Here, roleplay is only encouraged,
not required; therefore, this idea would not work.
</p></div><p style="font-style: italic">"We should be able to wear rings on more than two fingers"
</p><div style="margin-left: 20px"><p>If we implemented this, we would have to reduce the power of all rings
to something negligible because of balance reasons.  Also, two rings
is a good tradeoff between spam and realism.
