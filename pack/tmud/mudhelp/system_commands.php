<h2>System commands (Command Help)</h2><p>Apart from commands used to actually play the game, there are some
related directly to the MUD as a computer system and also used to
communicate with the Ainur.  These commands are:
</p><ul><li><span style="font-weight:bold"><a href="news.php">news</a></span> - Allows you to browse and read all the major news about the MUD
posted by the Ainur.
</li><li><span style="font-weight:bold"><a href="updates.php">updates</a></span> - Displays the minor gameplay updates made recently by
the Ainur.
</li><li><span style="font-weight:bold"><a href="uptime.php">uptime</a></span> - Displays the time since last reboot.
</li><li><span style="font-weight:bold"><a href="mudtime.php">mudtime</a></span> - Displays the MUD server's time (EST / EDT).
</li><li><span style="font-weight:bold"><a href="save.php">save</a></span> - Saves your character.  This is hardly needed unless you are
really paranoid about the MUD crashing (which really, really does not
happen often); your character is automatically saved every 15
minutes and when you quit anyway.
</li><li><span style="font-weight:bold"><a href="resume.php">resume</a></span> - Command for use in mailrooms; when you are writing a mail
you can be interrupted by combat (usually of PvP nature).  Once
you have dealt with your enemy, you may "resume" your mail text
editing.
</li><li><span style="font-weight:bold"><a href="mudvote.php">mudvote</a></span> - A command that reminds you about voting for the MUD on MUD 
listing sites: TopMudSites and MudConnector.  Note that it only
reminds; it doesn't vote for you!
</li><li><span style="font-weight:bold"><a href="vote.php">vote</a></span> - A command that brings up the player voting system, if there are
any pending votes.  The Administration can add them to poll players'
opinion of something or to conduct a survey.  Note that if there are
any pending votes while you log on, you will be "forced" into the
vote command (although with an option to quit right away), so there
usually is little need for you to use this command.
</li><li><span style="font-weight:bold"><a href="law.php">law</a>, <a href="harass.php">harass</a></span> - Commands for reporting rule-breaking to the 
Administration.
</li><li><span style="font-weight:bold"><a href="warnings.php">warnings</a></span> - Displays formal warnings issued by the Ainur to you for
your incorrect behaviour.  If you gather enough warnings, you risk
removal of your character from the game.
</li><li><span style="font-weight:bold"><a href="bug.php">bug</a>, <a href="idea.php">idea</a>, <a href="typo.php">typo</a>, <a href="commend.php">commend</a></span> - Submit a report about a problem you have
encountered in the game to the Ainur.
</li><li><span style="font-weight:bold"><a href="dev.php">dev</a></span> - Displays the game features that are currently in-development 
as well as lists the current administrative staff.
</li><li><span style="font-weight:bold"><a href="prop.php">prop</a></span> - Lists public Valinor proposals and lets you view them.  For
now, they are the past petitions submitted by players.  Note: the
petition system is no longer active.
</li><li><span style="font-weight:bold"><a href="survey.php">survey</a></span> - Lists and lets you vote and comment on various surveys 
posted by the Administration.
</li><li><span style="font-weight:bold"><a href="newbiehelper.php">newbiehelper</a></span> - The newbiehelper interface command.  This command 
lets you view the currently enlisted newbiehelpers and also lets
you apply to become one.
</li><li><span style="font-weight:bold"><a href="immort.php">immort</a></span> - Lets you apply to become an Ainu.  We constantly need new 
staff and will welcome your help in maintaining and expanding the
MUD.  Note that only experienced players are allowed to use this
command; we want our staff to know what they are working with!
</li><li><span style="font-weight:bold"><a href="hibernate.php">hibernate</a></span> - Locks your character account for a specified period
of time in case you need to take a longer break from playing.
It is not reversible, so think twice before using this!
</li><li><span style="font-weight:bold"><a href="suicide.php">suicide</a>, <a href="depart.php">depart</a></span> - PERMANENTLY destroys your character.
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
