<h2>Character commands (Command Help)</h2><p>Character customization is a thing we treat very seriously at The
Two Towers.  While the initial character creation is simple to get you
into the game as soon as possible, once you begin playing all the
customization options become available to you to be used at your
leisure.  The commands presented below let you control how your character
looks and behaves.
</p><ul><li><span style="font-weight:bold"><a href="score.php">score</a>, <a href="skills.php">skills</a>, <a href="tradeskills.php">tradeskills</a>, <a href="charinfo.php">charinfo</a></span> - The Two Towers is a 
combat-heavy game where you will be fighting for your life (and gold!)
quite a lot.  Combat is based on your stats and skills, so you should
be very well aware what their current values are.  These commands let
you query all the statistics about your character.
</li><li><span style="font-weight:bold"><a href="hp.php">hp</a>, <a href="hpq.php">hpq</a></span> - Quick shortcuts to display your current HP (hit points).
</li><li><span style="font-weight:bold"><a href="exp.php">exp</a></span> - Quick shortcut to display your current experience.
</li><li><span style="font-weight:bold"><a href="quests.php">quests</a></span> - This command provides you a detailed list of quests that
you have completed while playing the game.
</li><li><span style="font-weight:bold"><a href="spells.php">spells</a></span> - This command, of particular use to wizards and necromancers,
displays all the spells you currently know.
</li><li><span style="font-weight:bold"><a href="describe.php">describe</a>, <a href="undescribe.php">undescribe</a>, <a href="wardrobe.php">wardrobe</a>, <a href="pose.php">pose</a>, <a href="removetag.php">removetag</a></span> - One of the most 
important things that you have to do to define your character is to
tell us how he or she looks.  With these commands, you will be able
to write and toggle character descriptions and tags you receive
from quests.
</li><li><span style="font-weight:bold"><a href="languages.php">languages</a>, <a href="speak.php">speak</a>, <a href="speech.php">speech</a></span> - Another part of your character's roleplay
is how he or she speaks, and in which language.  These commands let
you view your current language proficiency and define the way you
will speak.
</li><li><span style="font-weight:bold"><a href="surname.php">surname</a>, <a href="storyedit.php">storyedit</a></span> - These commands let you define your character
background.  The simple thing is to just assign him or her a surname;
however, for those who want, we have an option to write a full
character story for other players to read.
</li><li><span style="font-weight:bold"><a href="guildinfo.php">guildinfo</a>, <a href="laston.php">laston</a>, <a href="legendinfo.php">legendinfo</a>, <a href="afk.php">afk</a></span> - The most common way other players
will learn of you is through the use of the "legend" command.  These
four commands let you slightly configure how your legend will look.
</li><li><span style="font-weight:bold"><a href="autoaccept.php">autoaccept</a></span> - This command lets you define how your character will
react to items given by other players.
</li><li><span style="font-weight:bold"><a href="serve.php">serve</a></span> - This Free Race-only command provides an option for a player
to pledge their service to Sauron.  Serving players lose their
affiliation with the "good" side of the war, and in many respects
will be treated as Evil Races.  This command can only be used in
the presence of Gothmog, commander of Sauron's forces outside of
Minas Tirith.
</li></ul><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
