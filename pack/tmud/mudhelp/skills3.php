<h2>Skills3 (Skills Help)</h2><p>There are two categories of skills in The Two Towers: common skills
and professional skills.  Every player has the common skills from the
beginning, and cannot lose them.  The professional skills, on the other
hand, must be obtained by either attempting to use them, or at a specific
professional trainer.  They can later be lost if you decide to change your
profession.
</p><p>You may have up to 8 professional skills at one time.  4 of them
always belong to your profession of choice, while the other 4 you may
pick from other professions as you see fit.  The skills of your profession
may be trained up to the value of 100; the 'other' skills may only reach
the value of 60.  Furthermore, the 'other' skills will also degrade at
a rate of 1 point per hour.
</p><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
<td style="border:0px;white-space:nowrap;" valign="top">Note:</td><td style="border:0px" valign="top"><div style="margin:0px;padding;0px;margin-left:10px;">
Since civilians and minions do not have their specific professional
skills, they can have 8 'other' skills chosen from other professions.
However, be aware that they still follow the rules for the 'other'
skills: they may be trained only up to 60 and degrade over time.

</div></td></tr></table><br/>
<p>The following skills are available:
</p><ol style="list-style-type:decimal"><li>Common skills
<br/><a href="aim.php">aim</a>, <a href="attack.php">attack</a>, <a href="awareness.php">awareness</a>, <a href="defense.php">defense</a>, <a href="dodge.php">dodge</a>, <a href="evasion.php">evasion</a>, <a href="fire_building.php">fire building</a>, <a href="intuition.php">intuition</a>, <a href="riding.php">riding</a></li><li>Warrior skills
<br/><a href="battle_instincts.php">battle instincts</a>, <a href="blocking.php">blocking</a>, <a href="combat_arms.php">combat arms</a>, <a href="tactics.php">tactics</a></li><li>Archer skills
<br/><a href="archery.php">archery</a>, <a href="deflecting.php">deflecting</a>, <a href="fletching.php">fletching</a>, <a href="positioning.php">positioning</a></li><li>Assassin skills
<br/><a href="assassination.php">assassination</a>, <a href="deception.php">deception</a>, <a href="lethal_precision.php">lethal precision</a>, <a href="stalking.php">stalking</a></li><li>Rider skills
<br/><a href="balance.php">balance</a>, <a href="motivation.php">motivation</a>, <a href="mounted_combat.php">mounted combat</a>, <a href="taming.php">taming</a></li><li>Thief skills
<br/><a href="locksmith.php">locksmith</a>, <a href="nimbleness.php">nimbleness</a>, <a href="stealth.php">stealth</a>, <a href="theft.php">theft</a></li><li>Ranger skills
<br/><a href="nature's_grace.php">nature's grace</a>, <a href="nature's_lore.php">nature's lore</a>, <a href="tracking.php">tracking</a>, <a href="wilderness.php">wilderness</a></li><li>Wizard skills
<br/><a href="6th_sense.php">6th sense</a>, <a href="concentration.php">concentration</a>, <a href="control_magic.php">control magic</a>, <a href="resist_magic.php">resist magic</a></li><li>Sniffer skills
<br/><a href="butchery.php">butchery</a>, <a href="prowling.php">prowling</a>, <a href="tracking.php">tracking</a>, <a href="wilderness.php">wilderness</a></li><li>Necromancer skills
<br/><a href="6th_sense.php">6th sense</a>, <a href="necromancy.php">necromancy</a>, <a href="warp_magic.php">warp magic</a>, <a href="will.php">will</a></li></ol><p>To see your list of skills, simply type "skills".  This will list your
current skill levels, to the right of the skill names.
</p><p>You can improve your skills in two ways:
</p><ol style="list-style-type:decimal"><li>Skills improve as you use them.  Each time you use a skill, you are 
credited with one 'use' of that skill.  When you get enough 'uses',
your skill goes up.  Some skills are invoked by commands, such as
repair and pick.  Others will be used when something requires them,
such as aim, attack, dodge and defense skills which will be used if
you are fighting a monster.  It takes more 'uses' to raise the skill
at higher skill levels.
</li><li>You may improve skills at a trainer, for a price.  While at lower 
skill levels, the 'by use' training may be enough, at higher levels
you will notice that it simply takes too much time - and that will
be the time to see the trainer!
</li></ol><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
