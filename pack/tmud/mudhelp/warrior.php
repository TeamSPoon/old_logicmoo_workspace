<h2>Warrior (Professions Help)</h2><p>"Last of all H&uacute;rin stood alone.  Then he cast aside his shield,
and wielded an axe two-handed; and it is sung that the axe smoked in
the black blood of the troll-guard of Gothmog until it withered, and
each time that he slew H&uacute;rin cried 'Aur&euml; entuluva! Day shall come
again!'"
</p><p>Does the idea of suiting up in chain mail, grabbing your trusty
sword, and wading into the midst of a battle appeal to you?  Does the
cleaving of limbs and severing of heads have a special place in your
heart?  Have you always dreamt of being the person who could walk into
any fight, unafraid, ready to kick butt, and trained to do so?  Are you
ready to stand alone against your foes with only your sword in hand and
your armour about you?  If you are up to the challenge, then perhaps you
should be a warrior.  All it takes is guts and determination.
</p><p>Warriors are the stuff legends are made of - what common folk dream
of being.  Disciplined, hardened fighters; masters of combat;
knowledgeable in all types of armaments and trained to be lethal with
them.  They are the heroes (or nightmares) of the realm.  Some have been
known to use their skills to serve only themselves, enslaving the weak
to build their manors, and slaying all who oppose them!
</p><p>Warriors possess several skills that aid them in combat. These are:
</p><ul><li>Battle Instincts
</li><li>Tactics
</li><li>Blocking
</li><li>Combat Arms
</li></ul><p>Warriors are also proficient at the following abilities:
</p><ul><li>Compare
