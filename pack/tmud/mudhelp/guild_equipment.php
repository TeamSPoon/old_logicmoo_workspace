<h2>Guild equipment (Guild Help)</h2><p>Guilds are allowed to have their own unique special equipment.  It
can only be obtained in their private guild shops.  Guild equipment is
a major roleplaying tool and also a display of guild pride and a sense
of belonging.  It can also come in quite handy at reboot when no other
equipment is readily available.
</p><p>The following rules pertain to guild equipment:
</p><ol style="list-style-type:decimal"><li>Your guild must have a shop in order to be able to order guild
equipment.
</li><li>You may order only one guild weapon and one guild pack.  For guild
armour, the livery system may be used.
</li><li>Once purchased, the guild weapon and pack will load into your shop
every reboot, and can be bought.  The livery system works differently.
</li><li>All guild weapons, armour and packs have the same power, size and
durability.  All guild items are equal.  Only the descriptions may be
altered through the GMedit system.
</li></ol><h3>Customizing Guild Equipment</h3><ol style="list-style-type:decimal"><li><span style="font-weight:bold">Weapons</span> - A guild weapon may be purchased for 100,000 gold through 
GMedit.  The guildmaster may fully customize the weapon's description
and emotes.  This can be done via GMedit as well.  A weapon can only
be of one type: bow, staff, sword, mace, spear, etc.  It cannot be both
a sword and a staff, or a staff and a spear, or any other combination.
</li><li><span style="font-weight:bold">Pack</span> - A guild pack may be purchased for 100,000 gold through GMedit.
The guildmaster may fully customize the pack's descriptions, but not
its size.
</li></ol><table border="0" cellpadding="0" cellspacing="0" style="border:0px"><tr>
