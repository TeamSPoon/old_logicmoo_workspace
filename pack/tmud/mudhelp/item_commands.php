<h2>Item commands (Command Help)</h2><p>During your travels in Arda you will come across many different
items for different purposes.  It is important to learn how you can work
with them; the commands listed below are those used in relation to items:
</p><ul><li><span style="font-weight:bold"><a href="inventory.php">inventory</a></span> - The main item-related command in game: displays the items
that you carry or the contents of a container.  Shortcut for inventory
is "i".
</li><li><span style="font-weight:bold"><a href="enc.php">enc</a></span> - Quick shortcut to display your current encumbrance.
</li><li><span style="font-weight:bold"><a href="gold.php">gold</a></span> - A shortcut command for displaying only your gold.
</li><li><span style="font-weight:bold"><a href="hands.php">hands</a></span> - A shortcut command for displaying only items held in your 
hands; this command can also be used to show in-hand items of another
living being.
</li><li><span style="font-weight:bold"><a href="get.php">get</a>, <a href="take.php">take</a>, <a href="put.php">put</a>, <a href="drop.php">drop</a>, <a href="retrieve.php">retrieve</a></span> - The item movement commands.  They
let you move items from place to place, such as from the room you are
in to yourself.
</li><li><span style="font-weight:bold"><a href="wield.php">wield</a>, <a href="wear.php">wear</a>, <a href="remove.php">remove</a>, <a href="shift.php">shift</a></span> - Commands that let you actually use the
weapons and armour you find.
</li><li><span style="font-weight:bold"><a href="sheath.php">sheath</a>, <a href="sling.php">sling</a>, <a href="store.php">store</a>, <a href="draw.php">draw</a>, <a href="unhand.php">unhand</a></span> - Commands that let you move your 
weapons and other items around your body, from containers such as
baldrics to your hands and back.
</li><li><span style="font-weight:bold"><a href="open.php">open</a>, <a href="close.php">close</a>, <a href="lock.php">lock</a>, <a href="unlock.php">unlock</a></span> - Commands that manipulate containers; they
can also be used with doors in rooms.
</li><li><span style="font-weight:bold"><a href="eat.php">eat</a>, <a href="drink.php">drink</a></span> - These are rather obvious!
</li><li><span style="font-weight:bold"><a href="light.php">light</a>, <a href="ignite.php">ignite</a>, <a href="extinguish.php">extinguish</a></span> - Commands that let you use light-emitting
objects, such as torches or magic crystals.
</li><li><span style="font-weight:bold"><a href="skin.php">skin</a></span> - One of the most-used golding commands: when you kill an animal,
be sure to skin its corpse and sell the pelt for money!
</li><li><span style="font-weight:bold"><a href="cook.php">cook</a></span> - Once skinned, animal corpses can be cooked to provide a decent
meal to restore your hit points.  However, you may also cook meat that
came from other sources; furthermore, "cook" is also an interface for
the cooking tradeskill for complex recipes.
</li><li><span style="font-weight:bold"><a href="empty.php">empty</a>, <a href="fill.php">fill</a></span> - Commands that deal with "drink" objects.
</li><li><span style="font-weight:bold"><a href="tag.php">tag</a>, <a href="untag.php">untag</a></span> - These commands let you mark items as unsellable, so that 
you do not accidentally leave them at the shop when you "sell all".
</li><li><span style="font-weight:bold"><a href="throw.php">throw</a></span> - Lets you throw an item around, or at another living being.
Throwing heavy items can cause considerable damage!
</li><li><span style="font-weight:bold"><a href="brew.php">brew</a></span> - Interface for the alchemy tradeskill; lets you brew a potion
from specified ingredients.
</li><li><span style="font-weight:bold"><a href="poison.php">poison</a></span> - A command that lets you use the various dangerous substances
you may come across.
</li><li><span style="font-weight:bold"><a href="packtag.php">packtag</a></span> - For use with mount containers (such as panniers) and wagons;
this helpful command lets you sell contents of your "stationary"
container without the hassle of going back and forth to the shop.
</li><li><span style="font-weight:bold"><a href="itemsort.php">itemsort</a></span> - Sort autoloading items one at a time in your inventory.
