Law and Order on The Two Towers is maintained through the Law System.
A player may steal from or even kill another player, but to do so is
a crime and can be reported!  The criminal will be fined a price in
gold relative to the severity of the crime.  The criminal must pay up,
or they will become a Wanted outlaw.  Once Wanted, any player can
attempt to bring in the fugitive.  When the fine is paid by the 
criminal, dead or alive, a portion of those fines will be given to the
victim as compensation.
<br><br>

<H2>The Crimes</H2>

<H3>Murder</H3>
Murder is the killing of another player.  If the victim is unsure
of which player killed them, they can ask a friend to investigate
them or investigate their killer.
<br><br>

Murder is not sanctioned, however it is rumoured that the assassins
of Arda have an underground network of contracted killers and
bribable judges.  When performing a murder-for-hire, the assassin
can bribe the judge to pay a reduced fine.  While it is a sure
death being on the receiving end of a contracted killer, you can make
this system work for you: if there is someone
bothering you that you can't manage to kill yourself, you can put
a contract on their head and let the experts handle it.
<br><br>

<H3>Attempted Murder</H3>
Attemped Murder is an attack on another player that does not result
in their death, either because the criminal called off the attack.
showed mercy and bandaged their fallen foe, or the victim managed to
evade and escape death.  All of these are still considered a crime
and the attacker can be reported for attempted murder.  However,
attempted murder leaves no evidence other than the victim's own 
witnessing the act, so attempted murder cannot be investigated.
<br><br>

<H3>Theft</H3>
Theft includes not only the stealing of another player's items or
gold, but also killing their horse.  Theft can be investigated, and
the fine will be based on the value of the stolen property.
<br><br>

<H3>Attempted Theft</H3>
Attempted Theft is when a player tries to steal from you but fails.
You may or may not notice the attempt.  Attempted theft cannot be
investigated, and the attempted killing of someone's horse does not
count as attempted theft.
<br><br>

<H3>False Report</H3>
Part of the intrigue of the Law is "whodunnit"?  Thieves and Assassins
will use various techniques to conceal their identity, in their attempts
to get away with their crimes.  To discover your assailant you may have
to turn to another player to investigate.  In the end you may end up
reporting the wrong player!  Reporting the wrong person is a crime,
if accused for a crime you did not commit you can file a False Report
charge in return!
