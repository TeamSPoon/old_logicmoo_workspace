<a name="connect"></a><h3>How do I connect to play The Two Towers</h3>
<br><br>
The Two Towers is played exclusively online.  Connecting to our server is easy,
either using our Java Connect applet or any standard telnet program.
Many of our users prefer a full featured mud
<a href="clients.php">client</a> for the added features
and convenience that these programs provide, however a client is not
necessary to play and enjoy the game!
<BR><BR>
<B>Windows users</B> - 
The standard telnet program that comes with Windows is not entirely
compliant with the telnet standard.  For Windows users we recommend our
Java Connect program or you can download a client.
<br><br>
<B>Mac OS X Users</B> - 
You have a standard telnet program already installed on your system.
In Finder, go to Applications and then down to the Utilities folder.
Launch the 'Terminal' application.
<i>For the best clarity we recommand you select "Window Settings..." from
the "Terminal" Menu and change the terminal from the default "Black on white"
to "White on black"</i>
<br>
At the prompt type:
<pre>telnet t2tmud.org 9999</pre>
<br>
<B>Unix and Linux Users</B> - 
From a standard terminal, type: 
<pre>telnet t2tmud.org 9999</pre>
<br>
Users on all platforms can also use our Java Connect from
the <a href="connect.php">connecting</a> page.
<BR><BR>
Once connected, you will see our 'castle' login screen, and can <a href="getstarted.php">create a new character</a>.
<BR><BR><BR>

<hr noshade><BR>
<a name="firewall"></a><h3>The connection times out and never connects!</h3>
<br><br>
The Two Towers server is on a reliable internet connection.  While it is
possible that there is an outage either with the server or the Internet,
it is far more likely that you are behind a firewall preventing you from
connecting.
<br><br>
If you are behind a firewall, try connecting to port 6667 instead of port
9999.  Many firewalls have this port open despite having port 9999 blocked.
<BR><BR><BR>

<hr noshade><BR>
<a name="nothing"></a><h3>I get to a prompt, and type, but nothing happens!</h3>
<br><br>
In your telnet program, you may have to tweak the following options:
CR/LF to CR (or vice versa), local echo (to on or off), and Set BS to DEL (on or
off).  Also remember that VT100 emulation will work, as will ANSI. Try both.
<BR><BR><BR>

<hr noshade><BR>
<a name="color"></a><h3>How do I see color?</h3>
<br><br>
Your Terminal emulation on your telnet client should be set to ANSI.
The Two Towers supports ANSI color in its text.  For help on color, type
'help color' once you have connected or click <a href="http://t2tmud.org/mudhelp/color.html">here</a>.
<BR><BR><BR>

<hr noshade><BR>
<a name="java"></a><h3>Java Connect does not seem to work for me - Why?</h3>
<br><br>
Ahh. The wonders of modern technology.  Simply put, either your browser is not
compatible with Java, you dont have enough bandwidth to run the Java Connect,
or your system resources are too low to run the Java Connect applet.
Or, it could just be a little flaky.  At any rate, it works on most
modern systems with a 4.0 MS or Netscape browser or better.
<BR><BR>
But if you have difficulties with it, you can always download a full
<a href="clients.php">mud client</A>.
<BR><BR><BR>

<hr noshade><BR>
<a name="unix"></a><h3>I also have a UNIX shell account... will this be faster for me?</h3>
<br><br>
If you are using a shell account, all you have to do is type: "telnet
towers.t2tmud.org 9999".  Some people may find that a straight telnet 
from their shell account is easier than going through a telnet client.
<br><br><BR>

<hr noshade><BR>
<a name="ban"></a><h3>When I try to connect, it says I have to register a character... why?</h3>
<br><br>
From time to time, we have to ask people from certain sites to register
their characters with us.  We usually do this after we have a rash of
"problem players" from a particular site.  Simply email us at the <a
href="contact.php">contact page</a> and we will do
what we can to get things squared away.

<br><br><BR>

<hr noshade><BR>
<a name="download"></a><h3>Can I download The Two Towers</h3>
<br><br>
No.  The Two Towers is played exclusively online, and is not a downloadable game, like other games you may have played. 

