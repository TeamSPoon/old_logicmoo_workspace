<h2>Current Guilds of The Two Towers</h2>

  <a target="_new" href="http://go.to/amarthatar">
<b>Aina Amarth</b>
  </a>
<br>
In the Unfinished Tales it was written: "[The Ithryn Luin] were founders
of secret cults and 'magic' traditions that outlasted the fall of
Sauron". The Aina Amarth is one of these secret cults of men. The Ithryn
Luin arrived around 2000 years before the war of the ring and this makes
the Aina Amarth a very old cult indeed.
<br><br>

  <a target="_new" href="http://www.amruin.com">
<b>The Amruin</b>
  </a>
<br>
Theoden King has ridden to aid Gondor in the battle on Pelennor fields.
To ensure the safety of Rohan while he is away, a group of riders stayed
behind to protect its borders.  Sworn to protect Rohan and sworn to
protect Theoden King the Amruin stand on guard.
<br><br>

  <a target="_new" href="http://www.barukkhazad-dum.com">
<b>Baruk Khazad-Dum</b>
  </a>
<br>
The Baruk Khazad-dum reside in Erebor, gathering strength in 
supplies and recruits. Soon these dwarves will return to 
Azanulbizar, fulfill Balin's dream, and reconquer the halls 
of their forefathers.
<br><br>

<b>The Beornings</b>
<br>
    In this third age of the Sun, there is a race of honorable 
northmen who guard the ford of Carrock and the High Passes in 
Rhovanion from the seemingly never ending orcs, goblins and wargs.
This group is named after its ancestor, the fierce warrior, the 
Great Skin-changer, the Shape-shifter, Beorn.  They call themselves
Beornings.
<br><br>

<b>The Corsairs of Umbar</b>
<br>
Few have lived to tell the tale of the mighty warship of 
Bethagan and the Corsairs of Umbar after seeing the infamous
black sails and smelling the telltale scent of Umbar's Finest
ale. The mighty warship Bethagan, crewed by the descendants
of the noble line of Castamir, was last seen sailing in the
rivers of South Gondor, leaving behind a trail of burning 
villages. The port city of Linhir has itself been attacked 
and partially plundered by the Corsairs, yet there are 
rumours that their ultimate intent is much more nefarious 
than the terror they've been spreading: To infiltrate Minas 
Tirith and prepare for the arrival of the main fleet of 
Umbar.  Long have they lain claim to the throne of Minas 
Tirith which their ancestors were denied during the 
Kin-Strife... but were they ever this close to fulfilling it?
<br><br>

  <a target="_new" href="http://www.freewebs.com/durbatuluk/">
<b>Durbatuluk</b>
  </a>
<br>
Commissioned by the Dark Lord as his security protectorate, the
Durbatuluk continue to exist in this capacity.  They maintain and protect
the land of Harondor as well as quell any uprisings that occur. 
<br><br>

<b>Fellowship of the Rimbor-Anim</b>
<br>
Having heard whispers in the trees of a powerful ring, 
The Fellowship has embarked on the ultimate quest: finding 
this ring, and using it to fortify itself as a true power in
Arda. Closest of friends, and the most ruthless of enemies,
they will let nothing stand in their way.
<br><br>

<b>The Galadhrim</b>
<br>
The Elvish Army of Lady Galadriel who reigns in Lothlorien. Fighting any
that attack or kill moral folk who live within the Woods, the Galadhrim
are principally archers but do have other professions scattered in their
ranks. Elves of all types are welcomed, as well as the occaisional
honourable man.
<br><br>

  <a target="_new" href="http://www.freewebs.com/thegondorsvengeance/">
<b>Gondor's Vengeance</b>
  </a>
<br>
Men of honour and distinction, sworn to defend Gondor and her allies.
They are the Elite Guard of Denethor, Steward of Minas Tirith, and they
fight the ceaseless battle to ebb the tide of evil flowing from within
Mordor. Recruits must be lvl 15+, heroic+ alignment, abide by all killing
restrictions, and must pass an interview given by a senior member of the
guild.
<br><br>

<b>Gor B&ucirc;rzgoth</b>
<br>
The Dark Lord's main bidding for the Uruk-hai of Gor Gor B&ucirc;rzgoth is to
transfer desperately needed equipment and weapons from throughout 
Arda to Commanders along main front lines of the Pelennor Fields, 
Lothlorien, Dale and Erebor.  Providing his greatest warriors with 
the best swords and armour available, he boosts the morale of his 
troops.  The life within the Gor B&ucirc;rzgoth is frought with danger.
They must remain vigilant for attacks from those of the free world 
desiring to stop them, pulling together as a group to accomplish 
the missions ordered of them.
<br><br>

  <a target="_new" href="projects/mirdain">
<b>The Gwaith-i-M&iacute;rdain</b>
  </a>
<br>
  The Gwaith-i-M&iacute;rdain are a fellowship of craftsmen and women devoted
to carrying on the traditions laid down by Celebrimbor and his Jewel-
Smiths during the Second Age.  They are masters of crafting in all forms,
from forging rings and other jewelry to crafting musical instruments and
weapons of death and beauty, all of which they are known to sell for the
right price.  These Elves and Dwarves are often reclusive, but are seen
in the world at large as not only the only true craftsmen's guild, but as
steadfast allies in the war against the traitor Sauron.
<br><br>

<b>Knights of Dol Amroth</b>
<br>
In Dol Amroth, within the region known as Belfalas. A regiment of Knights
and supporting men and women, under the command of the Lieutenant of the
Southern Guard, to protect Belfalas and Dol Amroth from enemy attacks.
While the main force of the Knights are in Minas Tirith under the command
of Prince Imrahil. These men and woman are of the Dunedain race,
honorable fighters all of them, warriors, rangers and archers.

<br><br>

<b>Meglivornth</b>
<br>
The Meglivornth are the mercenaries of arda.  The guild was
founded on the obsession of war, gold and treasure.  The Meglivornth
believe they are protected by their ancestors who visit them in wolf
form.

<br><br>

<b>N&aacute;r-i-Anor</b>
<br>
The N&aacute;r-i-Anor are a guild of ancient elvish origins. Formed when the sun
first dawned upon Middle Earth, its followers swore an oath to protect
Anor, the sun, in all its forms, to prevent its light being destroyed by
Morgoth. This oath has brought them to the defense of the White City,
once known as Minas Anor, in its time of greatest need. They fight to the
death to uphold their oath, the Flame of Anor burning deep within them.
<br><br>

<b>The Preservers of Tharbad</b>
<br>
Preservers of Tharbad is a merchant guild located centrally in West Arda
that consists of dwarves and elves that supply the free races
of Arda in its war with Sauron and evil.
<br><br>

<b>Rangers of Ithilien</b>
<br>
    The Rangers of Ithilien are the last remaining descendants
of the people who populated the ancient forest of Ithilien 
before it was overrun by the enemy. They are charged by the
Steward of Gondor himself to scout and protect what remains
of their forest, as well as assailing and slowing the forces
of Sauron at every available oppourtunity.
<br><br>

<b>Ranhoth</b>
<br>
    A large band of ruffians led by a shadowy figure only known as
'Sharkey' has taken over much of the Shire.  Many Hobbits are being
robbed and mistreated.  Believing that the time for open rebellion was
not ripe, a band of Shirefolk and a few allies decided to form a
underground resistance.  Rumour has it that they have a hideout somewhere
in the Hills of Scary.  They publish a newspaper called the Common Tater.
<br><br>

<b>Sons of Ulfang</b>
<br>
"...Yet neither by wolf nor by balrog, nor by dragon, would Morgoth
have achieved his end, but for the treachery of men." The Sons and
Daughters of Ulfang are his spiritual descendants and work to further
Morgoth's dominion over Middle-Earth from the shadows.
<br><br>

<b>Taril Haleth</b>
<br>
The history of Dunland begins with the folk of Haleth, an ancient and
proud branch of the Atani who defeated Morgoth's hordes but refused to
submit to the rule of elves and thus chose to live on their own in the
woods.  Later their descendants were named Dunlendings. During the ages
many have tried to subdue Dunland. Numenoreans, orcs, Dunedain - all
failed in their attempts to invade the hills. The guild of Taril Haleth
was formed by the tribes to guide and train the forces of Dunland, and to
secure the freedom of its population against the attacks and the
treachery of the outside world. 
<br><br>

<b>Udungul</b>
<br>
In the year of 2951 Sauron declared himself openly and started gathering
his power in Mordor. It took no more than twenty years to rebuild the
Dark Tower upon its old foundations in the Gorgoroth. An elite
military force was gathered to enforce the law of Sauron, calling
themselves the Udungul.  They seek to bring pain and fear to the
free races of Arda.
<br><br>

  <a target="_new" href="http://www.thevalacirca.com">
<b>The Valacirca</b>
  </a>
<br>
The Valacirca is a high moral fellowship of the Dunedain of the North:
the noble Ranger tribe of Aragorn.   Originally a vanguard for the Nine
Walkers, they now safeguard the Redhorn Pass in the company of elves and
select other races of mixed professions.  Thus united, the wide-ranging
Sickle of the Valar has long been a force of good, both defending the
Free and striking out against the Enemy, wherever he may be found.
<br><br>

<br><br>
<H2>Other organizations of note</H2>
  <a target="_new" href="http://www.freewebs.com/asturasartes/">
<b>Asturasartes</b>
  </a>
<br>
The most cunning and gifted of Necromancers in Arda have banded together
with the strongest and fiercest of the Dark Lord's warriors in order to
create a truly formidable force. Together they wreak havoc throughout the
Western lands, bringing the despicable Free Races to their knees. 
<br><br>

  <a target="_new" href="projects/smokers.1">
<b>The Smoker's Guild of Arda</b>
  </a>
<br>
A Community of adventurers with the love of a good pipe and and
pipe-weed.  All those who have picked up the hobby in the course
of their travels are welcome.
<br><br>

